<h1>Regulamin publikacji na stronie GNJ</h1>

<h2>Tekst</h2>

<ol id="regulamin-list">

<li>Tekst nie może zawierać wulgaryzmów, niestosownych treści,
kryptoreklam.</li>

<li>Artykuł musi być oryginalnym dziełem autora, cytowany tekst powinien
być wyróżniony.</li>

<li>Tekst artykulu maksymalnie <?php echo $this->textMax; ?> znaków łącznie ze spacjami.</li>

</ol>

<h2>Zdjęcia</h2>

<ol id="regulamin-list">

<li>Zdjęcia powinny być <B>ciekawe</B> tzn. nie może być kilku podobnych,
zdjęć nieostrych, zdjęć niedoświetlonych/prześwietlonych, nie mogą
zawierać golizny, autor powinien posiadać prawa autorskie do zdjęć.</li>

<li>Jeżeli na zdjęciach są przypadkowi ludzie, to przed publikacją
musisz mieć zgodę od osób widocznych na fotografii.
Pozwolenie nie jest potrzebne, jeżeli wykonywałeś zdjęcia na imprezie
masowej, chyba że regulamin danej imprezy stanowczo tego zabrania.</li>

<li>Zdjęcia wyłącznie w formacie JPG, aspect ratio 4:3, max. <?php echo $this->imageSizeMax; ?>MB.</li>

<li>Max. liczba zdjęć: <?php echo $this->imageMaxNr; ?>.</li>

<li>Każde zdjęcie musi posiadać <b>podpis i autora</b> (część podpisu).</li>

<li>plany i przekroje wyłącznie w formcie: GIF,PNG lub PDF.</li>

</ol>

<h2>Linki</h2>

<ol id="regulamin-list">

<li>Link zewnętrzny powinien wskazywać na treść, która się nie zmieni i nie zniknie.</li>
<li>Link nie może wskazywać postów (np. facebook, twitter, google+).</li>
<li>Max. liczba linków <?php echo $this->linksMaxNr; ?> (włączając filmy youtube,vimeo itp.).</li>

</ol>

