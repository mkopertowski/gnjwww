<h1>
	Sprzęt używany do nurkowań w jaskiniach
</h1>

<p>W nurkowaniu jaskiniowym używa się sprzętu, które bazuje na wyposażeniu płetwonurka na wodach otwartych. Jednak w tym sprzęcie wprowadza się kilka zasadniczych modyfikacji, które pozwalają uniknąć niebezpieczeństw środowiska.
<p>Najistotniejszą zasadą, jeśli chodzi o 
konfigurowanie sprzętu, jest <b>zdublowanie zestawów podtrzymujących 
życie</b> (oddychanie). W aparacie o obiegu otwartym ten zestaw składa się z butli i automatu o stopniach rozdzielonych wraz z manometrami. Nurek jaskiniowy musi posiadać co najmniej dwa takie oddzielne zestawy, tak by w razie awarii dowolnego elementu w jednym zestawie, drugi zestaw pozostał nienaruszony.
<p>Oświetlenie jest montowane na kasku. Muszą to być 
co najmniej dwie latarki o czasie świecenie dłuższym niż czas planowanego nurkowania. Używa się również latarek o mocy kilkudziesięciu watów, które są zasilane z zewnętrznych akumulatorów. Zamontowanie oświetlenia na kasku pozwala na swobodne operowanie obydwiema rękami. 
<p>Do specyficznego wyposażenia płetwonurka należą: 
kołowrotek z poręczówką, sekatorek i kompas. Kołowrotek pozwala na rozwijanie linki wskazującej drogę powrotu w zmąconej wodzie. Linka ma średnicę od 2,5 do 4 mm. Kołowrotek zawiera standardowo 100-150m linki. Sekatorek ogrodniczy umożliwia łatwe przecinanie linki za pomocą jednej ręki. Używanie noża nurkowego jest niewskazane ze względu na konieczność operowania nożem za pomocą 2 rąk. Kompas mocuje się na zewnętrznej powierzchni dłoni. Służy do określania azymutu kierunku do wyjścia. 
<p>Kombinezon wybiera się w zależności od temperatury wody. Przy temperaturze poniżej 10 st. C nurkuje się w kombinezonach suchych. Z reguły są to kombinezony suche piankowe (z neoprenu), które w przypadku przebicia i zalania chronią nurka przed nadmiernym wychłodzeniem. Przy głębokich nurkowaniach używa się suchych kombinezonów powłokowych (np. z trilaminatu). Często zimna woda i czas nurkowania zmuszają do stosowania grzałek ubieranych pod suchy kombinezon - elektrycznych lub chemicznych.
<p>Do wyrównywania pływalności i mocowania butli 
stosuje się jackety (np. typu skrzydło), lub specjalne worki wypornościowe, 
zakładane pod zestawy butlowe. Ważne jest, aby sprzęt ten był niezawodny i aby jego wyporność umożliwiała wynurzenie nurka w sytuacji zalania suchego kombinezonu.
<p>Ponadto używa się standardowego wyposażenia 
takiego jak maska, płetwy, komputer nurkowy. Płetwy zabezpiecza się przed 
zgubieniem za pomocą gumki na sznureczku, którą zakłada się na podudzie. Komputer nurkowy mierzy czas i głębokość oraz oblicza dekompresję. W przypadku nurkowań eksploracyjnych ważne jest, aby komputer posiadał możliwość odtworzenia profilu nurkowania. Przy długich nurkowaniach należy też pamiętać o zdublowaniu maski nurkowej. 
<p>Na dodatkowe wyposażenie składają się: tabliczka 
do pisania pod wodą, zestaw kilkunastu gumek, dodatkowe ciężarki, pałeczki świecące. Gumki uzyskuje się z pocięcia dętki samochodowej. Doskonale nadaje się do stabilizowania poręczówki oraz mocowanie różnego rodzaju wyposażenia. Pałeczki świecące to przeźroczyste pojemniki, które po przełamaniu wewnętrznej kapsuły zapewniają kilka godzin jasnego światła. Znakomicie spisują się jako awaryjne źródło światła.&nbsp; </p>
<p>Do najważniejszych zasad korzystania ze sprzętu 
należy tzw. reguła "jednej trzeciej". Cały zapas posiadanego powietrza dzieli 
się na trzy części, z których 1/3 przeznacza się na płynięcie w głąb, 1/3 na 
powrót, a pozostałą 1/3 zachowuje się jako rezerwę. Z obydwu posiadanych 
zestawów butlowych należy korzystać równomiernie zmieniając butlę po zużyciu 10 
bar. Dzięki takiemu postępowaniu i jednakowej objętości butli, w przypadku 
utraty całego powietrza w jednej z butli (nawet najdalszym punkcie penetracji), 
zapas powietrza w drugiej butli wystarczy na bezpieczny powrót. 
<p>Kolejną kluczową zasadą jest to, że każdy punkt, do którego dopływa 
się, musi być połączony za pomocą poręczówki w sposób ciągły z wyjściem na 
powierzchnię. Pierwszy punkt zastabilizowania poręczówki powinien zawsze 
znajdować nad wodą. Pod wodą poręczówka powinna być poprowadzona w ten sposób, 
aby można było jak najłatwiej poruszać się wzdłuż niej, uwzględniając charakter 
korytarza. Poręczówka powinna być naciągnięta, aby utrudnić zaplątanie się. Nie 
należy dopuszczać, aby na zakrętach poręczówka wchodziła w ciasne szczeliny, 
albo w miejsca, gdzie poruszanie się jest utrudnione. Aby zrealizować te cele 
należy poręczówkę odpowiednio stabilizować. Standardowo robi się to za pomocą 
gumek zakładanych na występy skalne. Gumki mocuje się do poręczówki za pomocą 
"główki skowronka". Innymi metodami stabilizowania jest przywiązywanie lub 
zostawianie na dnie dodatkowych ciężarków. 
<p>Podczas płynięcia w zalanym korytarzu nie wolno podciągać się na 
poręczówce, gdyż można zerwać punkty stabilizacyjne. Poręczówka służy tylko do 
wskazywania kierunku. Sytuacje awaryjne polegające na zgubieniu poręczówki lub 
zaplątaniu się rozwiązuje się za pomocą odpowiednich procedur autoratowniczych 
przy użyciu kołowrotka, sekatorka i kompasu. Podczas długich nurkowań stosuje 
się dodatkowe butle depozytowe, które zostawia się w po przepłynięciu pewnego 
odcinka. W ostatnim odcinku penetracji płynie się już bez dodatkowych butli 
utrudniających penetrację. 
<h3 align=center>Każde nurkowanie powinno być starannie 
zaplanowane i przemyślane pod względem skonfigurowania sprzętu i ilości 
niezbędnego powietrza (w tym na dekompresję).</h3>

