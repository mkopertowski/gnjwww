<p>Wiosną 2001 roku Włodek postanowił zmierzyć się z nowym wyzwaniem i rozpoczął  karierę nurkową. Po miesiącu wziął udział w Warsztatach Nurkowań Jaskiniowych, a niedługo później w wyprawie do Rumunii. Jako doświadczony grotołaz szybko rozwijał swoje nurkowe umiejętności. Odtąd został stałym towarzyszem większości wyjazdów i wypraw nurkowo - jaskiniowych. Po wypadku, w którym zginął  Wiktor Bolek  Włodek stał się czołowym animatorem naszego środowiska, najaktywniejszym polskim nurkiem jaskiniowym ostatnich lat, popularyzatorem tej formy działalności. Był członkiem Podkomisji Nurkowania Jaskiniowego KTJ PZA. Planował wyjazdy i wyprawy, namawiał innych do udziału w nich, motywował do działania samemu intensywnie nurkując. Był On zwolennikiem działania w stylu „alpejskim” tzn. krótkich, intensywnych, dokładnie zaplanowanych wyjazdów, nastawionych na realizację precyzyjnie określonych celów. Ten styl zaowocował nie tylko bardzo częstymi nurkowaniami, ale również przynosił sukcesy w eksploracji jaskiń. Włodek miał na koncie liczne nurkowania jaskiniowe na głębokości rzędu 100 m. Był zwolennikiem stosowania oddycharki (rebreathera), nurkował w zasadzie wyłącznie z jej użyciem. Jego Śmierć to wielka strata dla środowiska polskich nurków jaskiniowych. Pozostał na zawsze po drugiej stronie syfonu...
</p>

