<h1>
Włodek Szymanowski  - „Spryciula”	
</h1>

<p>W dniu 06 kwietnia br w czasie nurkowania w jaskini Goul du Pont w miejscowości Bourg St.Andeol we Francji zginął Włodek Szymanowski. Na swoją ostatnią wyprawę odszedł wybitny grotołaz i płetwonurek, niezawodny partner i  serdeczny przyjaciel.</p>

<p>Urodził się 1958 roku  we Wrocławiu.   Jaskiniami zainteresował się niedługo przed maturą po tym, jak wpadła mu w ręce książka Aurona Tatziefa  „W jaskiniach Pierre Saint Martin”. Na kurs dla początkujących organizowany przez Sekcję Grotołazów we Wrocławiu trafił po przeczytaniu ogłoszenia na Politechnice Wrocławskiej, na której kilka miesięcy wcześniej zaczął  studia. Nie przypuszczał chyba, że sport jaskiniowy, połączony ze wspinaczką,  a w późniejszym okresie z nurkowaniem będzie treścią Jego całego dorosłego  życia.</p>

<p>Koleżanki na kursie nadały Mu przydomek „Spryciula”, który przylgnął  do Niego na stałe. 
Włodek od początku dał się poznać jako człowiek przełamujący  schematy i stereotypy, przecierający nowe szlaki, często prowokujący w swoich zachowaniach.  Ponieważ wszystkiego w  życiu uczył się bardzo szybko, również w sporcie  jaskiniowym  w bardzo krótkim czasie zdobył najwyższe kwalifikacje.  Niebawem  stał się jedną z lokomotyw  działalności zarówno sportowej, jak i eksploracyjnej Sekcji Grotołazów. </p>

<p>Włodek  miał dar zjednywania sobie ludzi do swoich często szalonych  pomysłów  i na brak partnerów nigdy nie narzekał.  Wynikało to chyba z faktu, że był bardzo bezpośredni i otwarty,  a ponadto cechowała go  wielka  fantazja. Był przy tym człowiekiem szalenie pogodnym  i nastawionym optymistycznie do życia. Jego słynne: „nie przejmuj się” słyszeli od niego wszyscy, którzy  zwracali się o poradę w różnych trudnych sytuacjach. Uśmiech z jego twarzy nie znikał prawie nigdy.  W najcięższych momentach swoimi żartami potrafił rozładować atmosferę  i zmobilizować innych do dodatkowego wysiłku. Nie zapomnę nigdy jego postawy na licznych trudnych akcjach eksploracyjnych i szkoleniowych, chociażby takich jak ewakuacja Irka Jasiaka po jego słynnym locie w  Wielkiej Studni w 1981 roku.  Idąc na akcję ze Spryciulą można było być pewnym: nudno nie będzie! A przy tym wszystkim nie był człowiekiem beztroskim. Do wszystkiego, w co się angażował  przygotowywał się bardzo starannie,  cenił profesjonalizm  i w sporcie, i w pracy. 
</p>

<p>Zawsze stawiał na młodych  i na organizowane przez siebie wyjazdy tatrzańskie często zabierał ludzi o  niewystarczających  kwalifikacjach, ale mających  serce do jaskiń. Wywoływało to niekiedy sprzeciw starszych  kolegów i władz Sekcji, czym Spryciula specjalnie się nie przejmował.
</p>

<p>Chęć przecierania nowych szlaków i robienia czegoś, czego przedtem nikt nie robił  znalazła odzwierciedlenie również w  jego działalności zawodowej.  Nie wszyscy wiedzą, że należał do grupy pierwszych maklerów papierów wartościowych, którzy zaczęli pracować w Polsce  pod koniec lat 80-tych.  Założył w tym czasie i potem przez szereg lat kierował pierwszym we Wrocławiu prywatnym biurem maklerskim. Znalezienie inwestorów i współpracowników do tego pomysłu na  raczkującym rynku kapitałowym nie było łatwe, ale nie dla Spryciuli. 
</p>

<p>Jedynej rzeczy, której Włodek nie potrafił, to marnowanie czasu. On nie nudził się nigdy. Kiedy w ostatnich latach  miał mniej zajęć zawodowych w związku ze zmianą pracy, założył  swoją piękną stronę internetową, na której zaczął opisywać poszczególne etapy swojej kariery sportowej. Przy okazji zbierał i porządkował informacje i materiały dokumentujące historię Sekcji Grotołazów. Dzięki Niemu udało się ocalić od zapomnienia mnóstwo epizodów z życia wrocławskiego środowiska jaskiniowego. Zabrał się za pisanie części Kroniki Sekcji Grotołazów przed zbliżającym się 50-leciem jej powstania. Niestety tego zrobić nie zdążył.
</p>

<p>W ciągu 27 lat działalności Włodek  uczestniczył w dziesiątkach wypraw tatrzańskich. Brał udział w eksploracji przede wszystkim takich jaskiń, jak: Wlk. Śnieżna, Wlk. Litowrowa, Czarna, Naciekowa, Za 7 Progami,  Bańdzioch Kominiarski, Kasprowa Niżnia i  wielu innych mniejszych. Za granicą działał w jaskiniach Włoch, Szwajcarii, Hiszpanii, Słowenii, Francji, Austrii, Wlk. Brytanii, Czech, Słowacji, Rumunii. Uczestniczył (często jako kierownik) w  wyprawach  nie tylko Sekcji Grotołazów, ale także innych klubów, przede wszystkim z Gliwic, Sosnowca, Katowic i  Krakowa, a także Oxford Univesity Cave Club.  Wspinał się w skale i w lodzie w Tatrach, Alpach, Dolomitach, górach Norwegii, Szkocji, dotarł nawet na Mount Kenya. Wymienienie wszystkich Jego osiągnięć zabrałoby dużo miejsca,  szczegółowo są one opisane na Jego własnej stronie:  www.deepnhigh.sgw.wroc.pl.  Tutaj wymienię tylko najważniejsze osiągnięcia jaskiniowe:
</p>

