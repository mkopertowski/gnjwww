<p>Aby dojść do zalanego szybu trzeba najpierw pokonać ok. 200 m całkiem wygodnego korytarza. Na miejscu zobaczyłem dwa stykające się ze sobą szyby. Do jednego wrzuciliśmy szpeja z kamieniami na linie żeby uniknąć poręczowania w pionie. Ubrałem suchara, zarzuciłem na plecy zestaw 2x8L i zanurzyłem się. Woda miała przejrzystość ok. 2-3m - była w niej jakaś zawiesina. Na dole szybu zwiedziłem ślepą komnatę i po chwili poszukiwania popłynąłem właściwym korytarzem. Na całej długości spągu ciągnęły się szyny kolejowe. Na wysokości 1m woda wyraźnie rozwarstwiała się, poniżej była słaba widoczność i zawiesina, a powyżej widoczność ograniczał jedynie zasięg światła. W pierwszej chwili odniosłem wręcz wrażenie, że wynurzyłem się z wody - dopiero bąble wydobywające się z automatu uświadomiły mi, że wciąż jestem zanurzony. Po kilkunastu metrach dopłynąłem do zwrotnicy i rozwidlenia korytarzy. Zwiedziłem obie odnogi. W obu przypadkach w odległości ok. 5-6m od końca korytarzy woda robiła się krystalicznie czysta - aż się wracać nie chciało. Wynurzyłem się po 29 minutach.</p>
<p>Podsumowując, sztolnia w Tąpadłach niewątpliwie jest warta zwiedzenia. To bardzo interesujące miejsce do trenowania nurkowania jaskiniowego.</p>

