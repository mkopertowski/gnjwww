<h1>
	Tąpadła - 21.09.2003
</h1>

<p>Sztolnia chromitu w Tąpadłach, ze względu na swoje położenie,  jest obiektem dobrze znanym wrocławskim nurkom jaskiniowym. Wielokrotnie służyła również za poligon doświadczalny podczas corocznych warsztatów jaskiniowych. W tym roku, na początku lata, wejście do sztolni zostało zamknięte ze względu na mieszkające tam nietoperze. Po kilku rozmowach telefonicznych z kierownictwem parku krajobrazowego udało się ustalić, że od września sztolnia będzie otwarta. 
Korzystając z okazji i pięknej pogody umówiliśmy się na nurkowanie.
</p>
<p>Spotkaliśmy się o godzinie 10:00 przed polaną, podzieliliśmy dobytek pomiędzy Magdę, Wiktora i mnie i ruszyliśmy w kierunku sztolni. Nasz ekwipunek (a zwłaszcza butle) budził spore zdziwienie wśród licznych grzybiarzy...</p>

