<h1>
	Nurkowanie w Wielkich Kominach - Jaskinia Miętusia
</h1>

<p>Jeszcze w styczniu 2004 narodził się plan nurkowania w Wielkich Kominach jaskini Miętusiej. W dniu 24 stycznia odbył się uciążliwy transport sprzętu nurkowego. Akcja transportowa bardzo się przeciągnęła gdyż na powierzchni była tak duża ilość świeżego śniegu, ze torowanie i szukanie otworu zajęło nam kilka godzin. Gdy w końcu dotarliśmy do syfonu byliśmy tak wyczerpani ze postanowiliśmy odłożyć nurkowanie na dzień kolejny i wracać na bazę.</p>
<p>W dniu 25 stycznia odbyło się nurkowanie.</p>
<p>Przebieranie i przygotowanie poszło bardzo sprawnie dzięki platformie z desek nad syfonem.</p>
<p>Mimo iż woda była bardzo czysta z powodu ciasnoty postanowiłem się zanurzyć nogami do tyłu. Po kilkudziesięciu metrach pełzania w tył korytarzyka nagle rozszerza się a w prawo odchodzi cienka poręczówka do "podwójnego życia Weroniki". Do dna głównego ciągu płynie się już we właściwej pozycji bardzo obszernym korytarzem wypełnionym krystalicznie czystą wodą.</p>

<p align=center>
	<img src="./img/mietusia.01.2005_1.jpg" alt="Nurkowanie w Wielkich Kominach" height=375 width=500><br>
	<i>Nurkowanie w Wielkich Kominach</i>
</p>

<p>Warto wspomnieć ze cały główny ciąg jest zaporęczowany liną speleo. Poręczówka na dnie ustabilizowana jest porzuconym przed laty pasem balastowym. 
Po osiągnięciu głębokość 30,8 m wpłynąłem w korytarz na dnie wielkich kominów, którym dotarłem do miejsca, które okazało się połączeniem z ciasnymi kominami.</p>
<p>Powrót do domu odbywał się w niemal zerowej widoczności. Po prostu w tej części jaskini nie występują prądy wodne i  na ścianach i stropie zbierają się osady w dużych ilościach, które zostały poruszone przez wydychane powietrze. Na 6 metrach zrobiłem wymagany przystanek bezpieczeństwa i wynurzyłem się przy "platformie". Nurkowanie zajęło 26 min. natomiast transporty, retransporty, składanie-rozkładanie sprzętu, desaturacja itd., łącznie kilkadziesiąt godzin.</p>

