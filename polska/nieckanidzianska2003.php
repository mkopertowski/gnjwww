<p>Po dotarciu do niewielkiej, porosłej trawą kotlinki koło Siesławic gdzie znajdowała się większość potencjalnych celów nurkowania rozpoczęliśmy akcje nurkowe. Pierwszym stało się niepozorne jeziorko, które wg kolegów z Speleoklubu Świętokrzyskiego mogło mieć podwodne połączenie z którąś z położonych nieopodal jaskiń. Faktycznie około 2,5 m pod lustrem wody natrafiono na otwór o owalnym przekroju w skale, skąd korytarz opadał stromo do głębokości 8 m, gdzie tworzył przewężenie niemożliwe do sforsowani</p>

<p align=center>
	<a href="./img/nn_studniagarbka.gif" target="_new">
		<img src="./img/nn_studniagarbka_small.gif" alt="Studnia Garbka" height=261 width=272></a><br>
	<i>Studnia Garbka</i>
</p>

<p>Nadmienić w tym miejscu należy, iż zarówno suche i zalane partie jaskiń, jak i różnego rodzaju powierzchniowe zagłębienia terenu na Niecce Nidziańskiej wykorzystywane są skwapliwie przez miejscową ludność jako wygodne, bo blisko położone i darmowe śmietniki. Nie inaczej było i tym razem. Dno jeziorka oraz spąg wspomnianego wyżej korytarza pokrywała dość gruba warstwa mułu z zalegającymi w nim dużymi ilościami gałęzi oraz różnej maści śmieci, wśród których niemożliwym stawało się prawidłowe zastabilizowanie poręczówki. Sprawę komplikowała dodatkowo niewielka, poniżej 1 m przejrzystość wody, która po kilku wykonanych ruchach natychmiast spadała do zera, oraz czarna barwa skał gipsowych w zalanych partiach. Odkryta jaskinia, za propozycją Jacka Gubały, nazwana została <b>Studnią Garbka.</b></p>
<p>Następnym celem była Jaskinia Wodna w Siesławicach, powstała z połączenia dwóch sąsiadujących, do niedawna odrębnych jaskiń Schroniska Wodnego II oraz III.</p>
<p>Połączenie w/w jaskiń nastapiło skutkiem obniżenia się poziomu wody, co sprawiło że pod mostem skalnym, dzielącym obie jaskinie utworzył się kilkudziesięciocentymetrowej wysokości przesmyk, łączący oba Schroniska. Zagadką pozostawała natomiast kwestia istnienia studni, wykrytej podobno przez członków Speleoklubu Święktorzyskiego podczas sondowania drewnianą tyczką zalanych partii Schroniska Wodnego II.</p>

<p align=center>
	<a href="./img/nn_schroniska.gif" target="_new">
