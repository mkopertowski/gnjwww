	<i>Schronisko Wodne II/III</i>
</p>

<p>Istnienie owej studni okazała się faktem; miała przekrój mocno rozciągniętej elipsy długości około 3m oraz szerokości 0,7m. Schodziła ona w takim kształcie do głębokości 4m, gdzie rozchodziły się od niej trzy korytarze, położone względem siebie mniej więcej pod kątem prostym. Pierwszy z nich schodził stromo do głębokości 8m, gdzie dalsze przemieszczanie uniemożliwiała zaklinowana, sporych rozmiarów opona (!), drugi opadając nieco łagodniej prowadził do niewielkiej salki, której jednakże pełnych rozmiarów nie udało się stwierdzić (fatalna widoczność i przeszkody naturalne i sztuczne), osiągając tu także maksymalną głębokość 8m, trzeci natomiast zwężał się i klinował na głębokości około 5m. Jest wielce prawdopodobne że w jaskini tej możliwości eksploracji nie zostały do końca wyczerpane. Nie stwierdzono natomiast żadnych możliwości podczas krótkiego rekonesansu nurkowego w położonej obok Schronisku Mokrym, gdzie zalana studzienka kończyła się ślepo na głębokości około 2 m.</p>

