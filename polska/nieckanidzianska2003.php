<h1>
	Notka o nurkowaniu na Niecce Nidziańskiej
</h1>
<p>W dniach 18-20.07.2003 odbył się wyjazd na tereny Niecki Nidziańskiej i okolic, mający na celu głównie eksplorację zalanych jaskiń Skorocic oraz Siesławic koło Buska. W wyjeździe tym wzięli udział Jarosław Niekludow (eksploracja nurkowa), Dariusz Bartoszewski (wsparcie transportowo, powierzchniowe) - obydwaj Sopocki Klub taternictwa Jaskiniowego z ramienia gospodarzy terenu – Speleoklubu Świętokrzyskiego - Jacek Gubała.</p>

