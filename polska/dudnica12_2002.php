<h1>
	Moje pierwsze nurkowanie jaskiniowe - Dudnica'2002
</h1>
<p>Po wielu próbach udało się nam z Wiktorem wreszcie zaplanować wspólne nurkowanie w Dudnicy. Wiktor wybierał się do Zakopanego na spotkanie Podkomisji Nurkowania Jaskiniowego i zgodził się mnie zabrać ze sobą.</p>

<p align=center>
	<img src="./img/dudnica12_2002_04.jpg" alt="Przebieranie się" height=333 width=500><br>
	<i>Przebieranie się</i>
</p>

<p>Do jaskini poszliśmy w sobotę 14.12.2002 w składzie: Wiktor Bolek jako kierownik zamieszania,  Włodek Szymanowski w roli transportu (spłaciliśmy dług dzień później taszcząc szpeje pod otwór Wielkiej Śnieżnej), Arnold Paliński z Justyną Błaszkowską i ja.</p>

<p align=center>
	<img src="./img/dudnica12_2002_26.jpg" alt="Justyna pomaga Arnoldowi" height=333 width=500><br>
	<i>Justyna pomaga Arnoldowi</i>
</p>

<p>Pogoda była piękna - mało śniegu i lekki mróz. Przebraliśmy się pod otworem (Wiktor nurkował "na sucho", Arnold i ja w piankach), sprawdziłem jeszcze raz automaty i zanurzyłem się w czystej wodzie. Pierwszy syfon, choć krótki, dostarczył mi sporych wrażeń. Okazało się, że nurkowania w jaskini nie da się porównać z żadnym innym sportem. Po prostu - to jest to! Po wynurzeniu się z wody usiadłem wygodnie i czekałem na resztę towarzystwa. Po pewnym czasie wyszedł się Wiktor, chwilę później Arnold. Niestety, jeden z automatów Arnolda odmówił współpracy, więc w stronę drugiego syfonu ruszyliśmy  z Wiktorem we dwójkę. Po przejściu krótkiego meandra i zejściu na dno szczeliny zobaczyłem wodę i znikającą pod jej powierzchnia poręczówkę. Sprawdziłem czy jest dobrze zestabilizowana i znowu woda zamknęła się nade mną.</p>

<p align=center>
	<a href="./img/dudnica_big.gif" target="_new">
		<img src="./img/dudnica_small.gif" alt="Plan syfonów w jaskini Dudnica." height=413 width=300></a><br>
	<i>Plan syfonów w jaskini Dudnica.</i>
</p>

<p>Na początku widoczność była słaba, ale już po pokonaniu przełamania syfonu (miły piaseczek) zobaczyłem piękny, długi i szeroki korytarz. Widok był piękny! Korytarz o wymiarach 2 x 3 metry biegł to w górę, to w dół, na dnie leżały kamienie, żwirek i piasek. Posuwałem się powoli żeby móc napawać się widokami. Po pewnym czasie (oczywiście - za krótkim) poręczówka przebiła taflę wody. Wynurzyłem się i tu - niespodzianka! Według Wiktora po drugiej stronie syfonu miała być wygodna półka, na której mogłbym się rozlokować i poczekać na niego. Zamiast niej zobaczyłem zaledwie skrawek skały i kolejną linę uciekającą pod wodę. Stanąłem przed dylematem: co robić? Przyszło mi do głowy, że może Wiktor uznał to miejsce za komorę powietrzną i powinienem iść dalej. Pewnie pomyślałem tak dlatego, że czułem wielki niedosyt nurkowania. Zerknąłem czy nie widać świateł Wiktora i zanurzyłem się. Studnia opadała pionowo, by po krótkim czasie przejść w pochylnię. Na dnie zobaczyłem poziomy korytarz o wysokości ok. pół metra. Tu postanowiłem zakończyć zabawę, wynurzyłem się i po chwili zobaczyłem światła Wiktora. Stwierdził, że woda podniosła się o parę metrów i studnia, która była zwykle pokonywana suchą nogą zamieniła się w jeziorko. Zanurzyliśmy się w niej ponownie.</p>

<p align=center>
	<img src="./img/dudnica12_2002_09.jpg" alt="Michał w Dudnicy" height=333 width=500><br>
	<i>Michał w Dudnicy</i>
</p>

<p>Niestety, woda już się zmąciła (przy mojej wydatnej pomocy) i Wiktor pokazał kciuk do góry. Rozpoczęliśmy powrót. Znowu płynąłem przez piękny korytarz Dudnicy, na który na pewno nie zwróciłbym uwagi podczas zwykłej akcji jaskiniowej. Jeden syfon, spacer, drugi syfon i na powierzchni powitało nas piękne słońce. Szybko się przebrałem, oderwałem wmarznięte w piankę kamienie i nabrałem ogromnego apetytu na rydze z patelni.</p>
<p>Nie ma dyskusji - nurkowanie w jaskiniach to jest właśnie to, co tygryski lubią najbardziej.</p>

<h3 align=right>Michał Gignal</h3>
