<p>Przy zerowej widoczności rozpocząłem powrót. Okazało się, że tuż przy zawale było trochę mało miejsca by się odwrócić. W toni wokół mnie pływało mnóstwo belek z obudowy. Wyczuwałem je dotykiem. Jedna z nich usilnie wchodziła mi pod pachę - chyba chciała się ze mną zabrać na powierzchnię. Na szczęście pozbyłem się jej. Rozpocząłem nawijać linkę z powrotem na kołowrotek. Posuwałem się bardzo powoli. Widoczność poprawiła się dopiero w miejscu, gdzie chodnik zwężał się. Minąłem 60. marker. Widziałem już do ok. 1 metra przed sobą. Bez problemu pokonałem zacisk w kałuży i wynurzyłem się.</p>

<p align=center>
	<img src="./img/dzikowiec_06_2003_2.jpg" alt="Po nurkowaniu" height=377 width=500><br>
	<i>Wiktor Bolek po wynurzeniu się z kałuży</i>
</p>
<p>Jak sprawdziliśmy później na powierzchni, ślady zawalonego chodnika były po drugiej stronie wzniesienia - na wysokości koryta strumyka. Do tego zawału musiałem dopłynąć od wewnątrz. Należy uznać, że eksploracja zalanych partii w tej sztolni została zakończona.</p>



