<h1>
	Sztolnia w Dzikowcu po raz drugi - czerwiec 2003
</h1>

<p>Na początku maja 2003 przeprowadziliśmy pierwsze nurkowanie w chodniku odwadniającym komorę wydobywczą w sztolni w Dzikowcu. Udało się wtedy przepłynąć nieco ponad 50m. Jednak w tym miejscu skończyła się nam lina, korytarz biegł jednak dalej.</p>
<p> Niecały miesiąc później wróciliśmy do tego problemu. W wyjeździe wzięli udział Wiktor Bolek, Michał Gignal, Michał Stajszczyk. Tym razem nawinąłem na kołowrotek 120 metrów poręczówki, co, jak miałem nadzieję, powinno tym razem wystarczyć.</p>
<p>Po zjechaniu do komory wydobywczej uderzył mnie dziwny wygląd spągu. Kamienie leżały w sposób jakby bardziej zwarty, a ponadto przykrywała je kilkumilimetrowa warstwa szarozielonego osadu. Spąg wyglądał jak dno zbiornika, z którego spuszczono wodę. Poświeciłem w kierunku jeziorek. Z przerażeniem nie dostrzegłem wody, zamiast niej była szarozielona powierzchnia. Czyżby podczas naszej miesięcznej nieobecności nastąpił gwałtowny przybór wody, który zamulił jeziorka i co najgorsze chodnik, który mieliśmy wyeksplorować? Wszedłem do jeziorka. Szarozielona zawiesina, przykrywająca dotąd szczelnie wodę, rozsunęła się i stałem do połowy kaloszy w wodzie. Odetchnąłem z ulgą. Pod zawiesiną woda była krystalicznie czysta. Wystarczyło ją tylko delikatnie odgarnąć z powierzchni, a dalej woda nadawała się doskonale do nurkowania.</p>

<p align=center>
	<img src="./img/dzikowiec_06_2003_1.jpg" alt="Powierzchnia jeziorka" height=377 width=500><br>
	<i>Szarozielona zawiesina, przypominająca krę, pokrywała powierzchnię jeziorka</i>
</p>

<p>Do nurkowania przebrałem się w gidrokostium, z którego uważnie spuściłem powietrze, w celu uzyskania neutralnej pływalności. Wziąłem dwie 4 l butle zamocowane po bokach. Ułożyłem się do zacisku w kałuży i wpłynąłem do chodnika. Widoczność nie była idealna. W wodzie unosiła się jakby lekka mgiełka, która ograniczała widoczność do 2-3 metrów. Pływalność miałem dobrze dobraną do głębokości 1m. Ze względu na zminimalizowanie ilości sprzętu nie używałem żadnego urządzenia do wyrównywania pływalności. Chodnik miał szerokość ok. 1m, dzięki czemu łagodnie odbijałem się nogami od ścian i szybko posuwałem się naprzód. Linka szybko odwijała się kołowrotka, tak że nie rejestrowałem kolejnych markerów. Po pewnym czasie pod stropem pojawiła się poduszka powietrzna - o której wspominał Michał Gignal po poprzednim nurkowaniu. Udało mi się nawet wystawić maskę ponad lustro wody. Strop jak i ściany były z litej skały. Popłynąłem dalej. Głębokość była ok. 1 metr - maksymalnie 1,8 metra. W pewnym momencie korytarz rozszerzał się. Musiał mieć ponad 1,5 metra szerokości, bo nie sięgałem już oboma nogami do ścian. Zacząłem podciągać się wzdłuż jednej ze ścian. Ale nie mogłem już tak dobrze utrzymywać równowagi i zacząłem coraz bardziej mącić wodę. Przy ścianach pojawiły się drewniane stęple podtrzymujące strop. Jeden z nich sterczał ukośnie w przekroju korytarza. Musiałem go przemieścić, aby przepłynąć dalej. Miał dosyć dużą bezwładność. Na ścianach pojawiła się szczelna drewniana obudowa. Na dnie znajdował się czarny osad - musiała to być gleba. Po chwila ta gleba wypełniła cały przekrój korytarza. To był koniec eksploracji. Dopłynąłem do zawału. Zanim woda kompletnie się zmąciła zauważyłem, że marker 80. jeszcze się nie odwinął z kołowrotka.</p>

