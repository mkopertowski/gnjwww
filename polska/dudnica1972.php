<p>Ostatnim, który wyszedł z "dziury" tak pieszczotliwie nazwaliśmy Dudnicę był Witek. Ale nie tylko jemu należy się uznanie za godziny spędzone w wilgoci i zimnie za I syfonem w oczekiwaniu na tych, co tam, po drugiej stronie.</p>
<p>Gienek Maciańczyk z Klubu Podróżnika WSR pomógł nam bardzo dużo, podobnie jak nasz węgierski kolega - Jurik (spolszczona wersja niechrześcijańskiego imienia). Grażyna była za to nieoceniona jako ochmistrzyni.</p>
<p>Kulturalne życie na obozie zwieńczone było skromną świąteczną wieczerzą w 
licznym gronie (Gazdów Kubińców). Mieszkali u nich razem z nami koledzy z gdańskiego "Rekina" - będący na urlopie wypoczynkowym i pięć osób z Klubu 
Podróżnika z WSR. Odbyło się emocjonujące nie tylko z racji paskudnej pogody przejście Czerwonych Wierchów pod kierownictwem Teresy.</p>
<p>Żywiołowo rozwijał się słownik obozowy pojawiło się nowe i cenne określenie: "Fornal". Ustanowiono nawet Order Przechodni Fizycznego przyznawany honorowo za imprezy wyjazdowe. (Bliższe wyjaśnienia w kronice kulturalnej).</p>
<p>Zdobyte doświadczenie pozwoli na przygotowanie się do następnych akcji eksploracyjnych w jaskiniach. Dudnica czeka na nas jeszcze ze swoimi syfonami. Przed zimową akcją popracujemy solidnie nad sprzętem, bez którego trudno marzyć o realizacji jakichkolwiek poważniejszych zamierzeń. Zapał ludzi nie jest wystarczająca gwarancją bezpieczeństwa i powodzenia. Ale jego przynajmniej nie brakuje.</p>
<p>Pierwsza wyprawa jaskiniowa: Zakopane 30.03 - 4.04.72</p>
