<h1>
	Dudnica 1972 - pierwsze sekcyjne nurkowanie.
</h1>
<p>O nurkowaniu w jaskiniach mówiło się w naszym klubie od dawna. Dziś mamy już za sobą pierwszą, może trochę nieśmiałą, ale zakończoną pełnym sukcesem akcję. Sukcesem naukowym - wykonaliśmy pomiary, opis i dokumentację (plan, fotografie) części jaskini; sukcesem organizacyjnym (nie dostaliśmy ani grosza z Rady Okręgowej, a jednak wyszło) i szkoleniowym - ośmiu spośród dziewięciu płetwonurków przeszło przez jakiś syfon, choć jedynym mającym wcześniejsze doświadczenie był Norbert (uciekał kiedyś na bezdechu z zalewanej przez wodę Zimnej!).</p>

