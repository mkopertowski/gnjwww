<p>Dwanaście godzin pozwoliło nam wszystkim zapoznać się z dostępnymi informacjami na temat Bystrej i Dudnicy.</p>
<p>To dwie jaskinie, których otwory znajdują się na zboczach [...] Turni w odległości około 15m od siebie i stanowią element systemu odwadniającego masyw Giewontu. Bystra, zbadana i opisana na odcinku 600m, ma 14-metrową studnię wejściową, dość obszerne korytarze i kończy się niezbadanym do dziś syfonem. Planowaliśmy próbę jego przejścia i dalszej penetracji. Otwór Dudnicy jest bardzo niepozorną dziurą, a ciasny kilkumetrowy korytarz zamyka lustro pierwszego syfonu. Ile tych syfonów jest w Dudnicy - nie wiadomo. Tadeusz Zwoliński, odkrywca wielu jaskiń tatrzańskich pokonał kiedyś pierwszy syfon i suchą nogą dotarł do drugiego. W 1969 roku studenci z klubu płetwonurków "Alga" (Gliwice) doszli aż za trzeci syfon, ale musieli wołać GOPR, choć wszystko dobrze się skończyło. Planu jaskini nie sporządzono. Myśleliśmy o ewentualnej dalszej penetracji i wykonaniu opisu dokumentacyjnego.</p>
<p>Po rekonesansie w dniu przyjazdu wybraliśmy Dudnicę - Bystra była zalana, sądząc z poziomu wody w studni, na odcinku nawet może kilkuset metrów. 
Techniczne czynności jak transport sprzętu (a wypadało po plecaku lub aparacie na człowieka), poszerzanie zasypanego błotem i śniegiem otworu, zakładaniu bazy przed pierwszym syfonem, kosztowały nas sporo pracy.</p>
<p>Akcja ruszyła ostatecznie 1 kwietnia. Pierwsza trójka w składzie: Norbert, Piotr i Witek przeszła pierwszy syfon i... zatrzymała się na drugim. Był 
głęboki, za głęboki. Zanurzali się i bezskutecznie walczyli z siłą wyporu. W końcu Witek - ten z najmniejszą wypornością założył wszystkie pasy obciążnikowe i przeszedł. Syfon był niebezpieczny, z ciasnym gardłem w najgłębszym miejscu. Witek sam dotarł do trzeciego, przeszedł go także i zatrzymał się nad studnią, której w pojedynkę i bez liny nie mógł już sforsować. Następnego dnia wyszli na powierzchnię pod wieczór: Andrzej, Ludwik i Teresa - (jedyna nienurkująca osoba w naszym gronie) - w charakterze grupy pomiarowej, zaopatrzeni w taśmę i przyrządy do rysowania, przeszli pierwszy syfon i przez pięć godzin mierzyli. Była to niebagatelna zważywszy fantazyjne kształty w Szczelinowej Sali za pierwszym syfonem. Owocem jej jest plan i opis sporządzony przez Andrzeja. Ostatnią dwójkę, która trzeciego dnia zwijała sprzęt, transportowała go powrotną drogą przez I syfon i fotografowała to co się dało, stanowili: Krzysiek i Marek. Oni mieli też okazję do wypróbowania nowej metody przechodzenia syfonów, tzn. leżenia na plecach, z ustami nad wodą - niestety metody przydatnej jedynie dla I syfonu w Dudnicy, który okazał się być po prostu jeziorkiem z małym prześwitem nad lustrem. Że pływanie w jeziorku niewiele ma wspólnego w tym samym jeziorku, niżej podpisany przekonał się na własnej skórze.</p>

