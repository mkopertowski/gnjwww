<h1>
	Jaskinia Dudnica
</h1>
<h2>
Pierwszy film podwodny nakręcony w syfonach w Polsce
</h2>
<p>14 marca odbyła się akcja do jaskini Dudnica zorganizowana przez Sekcję Grotołazów Wrocław oraz TVP3 oddział Katowice. Głównym celem tej akcji było nakręcenie pierszego w Polsce filmu w syfonie znajdującym się w naturalnej jaskini. Ze strony Sekcji wzięli w niej udział nurkowie: Wiktor Bolek - kierownik oraz Rafał Górecki; ekipa wspomagająca w składzie Andrzej Dąbrowski, Łukasz Smoliński i Jerzy Haładus. Z ekipy TVP3 pod kierownictwem Ewy Żerdzińskiej w jaskini zanurzał się Wojciech Jeznach. Operatorem wynajętym do filmowania w wodzie pod stropem był Słowak : Pavel Svitanek.</p>
<p>
Jaskinia Dudnica znajduje się w Tatrach i jest stounkowo łatwo dostępna. Jednak samo dojście ze szlaku pod otwór wymagało torowania w śniegu powyżej kolan. W miejscu, gdzie zwykle odnajdywaliśmy otwór, zastaliśmy białe śnieżne pole. Odwilż panująca w całym kraju jeszcze nie dotarła do tego miejsca. Po kilku minutach nerwowego kopania udało się nam jednak namierzyć otwór. Przekop dla potrzeb telewizji poszerzył Jurek Haładus.</p>
<p>
Okazało się, że poziom wody w jaskini jest bardzo niski. Pomimo gwałtownych roztopów w Zakopanem, na wysokości 1100 m.n.p.m. dalej królowała zima. Pierwszy syfon stał się jeziorkiem i do drugiego syfonu można było się dostać bez nurkowania. W ciągu miedzy syfonami nie było żadnego przepływu. Brakowało charakterystycznego dla tej jaskini dudnienia wody. 
</p>
<p>
Ja wraz z Pavlem mieliśmy kręcić film w II i III Syfonie. Druga dwójka nurków: Rafał i Wojtek miała za zadanie pomóc nam w transporcie, a następnie sfilmowanie niedostępnej szczeliny w I Syfonie. 
</p>
<p>Po przetransportowaniu sprzętu do II Syfonu okazało się, że poziom wody jest niższy o ok. 2m od tego, który spotykaliśmy tu podczas poprzednich nurkowań. Samo wejście do jeziorka było strome. Jako pierwszy w syfonie zanurzył się Pavel, któy miał nakręcić jak najwięcej zdjeć w niezmąconej wodzie. Jednak przy przechodzeniu przez przewężenie na dnie tego syfonu widoczność spadła do ok. 0.5m. Popłynąłem zaraz zanim. Dzięki niskiemu poziomowi wody Syfony II i III były rozdzielone. Spokojnie wynurzyliśmy się w komorze. Przez niewielki próg przeszliśmy do III Syfonu, który nie miał kontaktu z wcześniej zmąconą wodą w II Syfonie. Nie pomyliliśmy się. W III Syfonie widoczność była idealna - około 10m. Dzięki wspaniałem u oświetleniu, które miał na kamerze Pavel mogłem nareszcie zobaczyć ten syfon w całej jego krasie. Nakręciliśmy parę ujęć podczas poruszanie się wzdłuż poręczówki. Za III Syfonem stanęliśmy na krawędzi Tajemniczej Studni. Nie było w niej ani śladu wody. Jak pamiętam z poprzedniego nurkowania, ta studnia była wypełniona wodą po samą krawędź. Po wykonaniu paru ujęć rozpoczęliśmy powrót. W miejscach, gdzie się wchodzi i wychodzi z syfonu woda zmąciła się. Jednak zrobiliśmy jeszcze parę ujeć w środkowej części syfonu, gdzie widoczność była najlepsza.
</p>

<p> Po powrocie do I Syfonu okazało się, że nasi koledzy nie byli w stanie nakręcić tam żadnych zdjęć. Woda w tym syfonie była kompletnie zmącona. Brak dopływu świeżej wody unimożliwiał odnalezienie "niedostępnej szczeliny".
</p>

