<h1>
	Tąpadła - 18.10.2003
</h1>

<p>Przed nurkowaniem w Syfonie Mysi w Jaskini Wielka Śnieżna postanowiłem przeprowaić nurkowanie treningowe w sztolni. Ze wzgledu na znaczną głębokość Syfonu Mysi - przynajmniej 15 - 20 metrów musiał zamontowac zawory dodawczy i nadmiarowy do gidrokostiumu. Gidrokostium to suchy kombinezon nurkowy produkcji ZSRR wykonany z gumy. Dzięki swej rozciągliwości i niewielkiej objętości doskonale nadaje się do nurkowaniach w syfonach, które wymagają pokonywania męczących i ciasnych odcinków w korytarzach wypełnionych powietrzem. Jak dotąd w płytkich syfonach nurkowałem w tym gidrokostiumie bez dodawania powietrza, bo syfony były płytkie. Problem zainstalowania standardowych zaworów do kombinezonu suchego polegał na tym, że te zawory nie powinny utrudnić pokonywania ciasnych miejsc. Stąd też zawór dodawczy zamontowałem z boku, a nadmiarowy po zewnętrznej części ramienia. Ciasne wejście do Syfonu Mysi wymaga zastosowania konfiguracji bocznej, natomiast ze względu na znaczną głębokość wymaga to zastosowania większych butli np. 2x 7l zamiast zwykłych 2x 4l. 
</p>
<p>Ostatecznie w akcji pomagał mi tylko Andrzej Dąbrowski (Senior). Sprawnie donieśliśmy sprzęt do zalanego szybu. Przebrałem się w zmodyfikowany gidrokostium i założyłem  [<a class="navismall" href="../szkolenie/sidemount_pl.php">konfigurację boczną</a>]. Zanurzyłem się do dna szybu. Przywiązałem poręczówkę do drabiny i popłynąłem do chodnika. Podczas manipulowania zaworem nadmiarowym poczułem jak zalewa mi się rękaw. Wobec tego całkowicie zakręciłem ten zawór. Zalewanie chyba ustało. Widoczność w poziomym chodniku była nienajlepsza 1-1,5 metra. Poza początkowym odcinkiem chodnika nie było widać rozwarstwienia wody - charakterystycznego dla tej sztolni. Poprzednie nurkowanie wykonaliśmy tu miesiąc wcześniej. Czyżby oznaczało to, że woda wymaga dłuższego okresu czasu by rozwarstwienie było widoczne? Czy też ktoś jeszcze zanurkował w tej sztolni w tym okresie czasu? Reszta nurkowania przebiegłe bez problemów. Wynurzyłem się po 25 minutach.  </p>

