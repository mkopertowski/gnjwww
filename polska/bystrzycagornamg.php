na mnie - w końcu to ja chciałem tam zanurkować. </p>
<p>
Po pokonaniu tej niewielkiej przeszkody pozostało nam tylko przejść kilkadziesiąt metrów poziomego korytarza i już staliśmy nad krystalicznie czysta wodą. Sztolnia ta nie kryje żadnych wąskich miejsc, więc nurkowałem z zestawem 2 x 8L na noszaku.</p>
