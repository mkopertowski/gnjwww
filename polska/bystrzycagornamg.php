<h1>
	Sztolnia w Bystrzycy Górnej - nurkowanie poznawcze
</h1>

<p>


Po udanym zakończeniu eksploracji sztolni w Dzikowcu postanowiliśmy razem 
z Wiktorem Bolkiem i Michałem Stajszczykiem wybrać się do sztolni w Bystrzycy Górnej. W tym miejscu Wiktor chce poprowadzić nurkowania w ramach tegorocznych warsztatów z nurkowania jaskiniowego. Wcześniej postanowił sprawdzić, jak wygląda wejście do sztolni. Skorzystałem z okazji, by zanurkować w nowym dla mnie miejscu.</p>
<p> 
