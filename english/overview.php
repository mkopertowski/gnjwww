<h1>
	Cave diving in Poland at the end of XXth century.
</h1>

<p>Cave diving in Poland is not an easy job. There is no big resurgence in our country. Most diveable sumps are placed in Tatra Mountains on the southern border. There is an alpine type karst; the temperature is about 3 deg. C. The sumps are deep in the caves - usually several hundreds meters below the entrance. The cave diver must be extremely fit to get there with all his equipment.</p>
<p>Main diving places are at bottoms of big vertical caves such as Miętusia, Śnieżna Studnia, System Wielkiej Śnieżnej-Litworowej. There are also some sumps in horizontal caves such as Kasprowa Niżnia, Jaskinia Bystrej, Dudnica, Zimna. The development of exploration in last four years is covered in this paper.</p>
<p>The deepest cave dives took place in Mietusia cave. There are two separate, vertical sequences of the pits at the down parts of this cave. One of them is called Wielkie Kominy (Great Chimneys) and the second Ciasne Kominy (Narrow Chimneys). For many years these two sumps remained unconnected, but many clues indicated that the connection is possible. The exploration in Narrow Chimneys was undertaken by cavers from club Sekcja Grotołazów Wrocław. The bottom of the last pit is divided into two separate lakes. Wiktor Bolek explored the south one. Within the years 1996-98 four assaults were made. On the dive on 29.12.1998 the depth of -70m was reached at the distance of 135m from the lake surface. It was a definite end of the exploration. The dive lasted 76' including 53' decompression. Trimix was used as bottom gas, air as travel mix and oxygen for final decompression.</p>
<p>Norbert Ziober explored the sump under the North Lake. At first sump went to the north, but then it turned to south in the direction of Great Chimneys. On the dive on 29.12.1998 Norbert found a guideline and a weight belt, which were placed in the sump from the Great Chimney's side. The distance from the northern lake to the connection point is about 130m with maximum depth of -29m.</p>

