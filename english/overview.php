</p>

<p>Cavers from Speleoklub Warszawski (Warsaw) have been exploring the sump in Great Chimneys since early 70s. In that time the vertical part was descended to -30m and a horizontal tunnel was discovered. There is a connection point with Narrow Chimneys in this tunnel, but it was discovered almost 30 years later. In the second half of 90s Krzysztof Starnawski explored more underwater passages in this sump. At the beginning of 1996 he found a side tunnel at the depth of -12m. He swam there the distance of 180m at max depth of -21m to the dry gallery called Podwójne Życie Weroniki (Dual Life of Veronica). In February of 1997 Krzysztof and Andrzej Czubalski swam through this sump once again and climbed the meander in the gallery and found next sump Klarowny (Clear). Krzysztof dived it through in 1999. It was 15m long. A new dry gallery was found of length 50-60m. But it was a dead end of exploration in this branch.</p>

<p align=center>
	<img src="./img/overview_1.jpg" alt="A diver in one of sumps of Jaskinia Kasprowa Niżnia" height=375 width=500><br>
	<i>A diver in one of sumps of Jaskinia Kasprowa Niżnia</i>
</p>

<p>A bit of a new underwater terrain has been explored recently in Jaskinia Bystrej. This cave is a complex horizontal one. There are many minor sumps, but the place for diving is a sequence of five sumps in the main passage. The first four were known from early 70s. The exploration in the fifth was stopped in 1987, when Aleksiej Petrujkic drown there during exploration. The exploration in this sump has been recently undertaken once again by Krzysztof Starnawski. In bad conditions (low visibility and tight space) he was able to pass 200m with max. depth of -28m in Vth sump. He emerged in a small chamber. The Sump VI started there, but the entrance (vertical slit) was too narrow.
Several diving attempts were made in the sumps, which are at the bottom area of System Wielka Śnieżna-Litworowa - presently the deepest cave in Poland (814 m). In this cave the main steamway goes through three sumps at the bottom: Dominika's Sump, Beata's Sump and sump Lake "X" (the deepest point in this system). They were explored in 70's and 80's. The way through Lake "X" was not found due to low visibility. The problem laid in Dominika's Sump, which was muddy and narrow. The stirred up mud in this sump went to last sump and destroyed visibility. After many years of exploration a new dry passage to the sumps was found. The Dominika's Sump was by-passed through Dziadek's Sump. It allowed undertaking exploration in Lake"X" in clear water. Maciej Tomaszek made the assault from STJ KW Kraków. He swam 30m in the last sump. At the depth 7m he arrived at sump's deflection, which was choked by boulders. He tried to remove them, but the mud was stirred and he had to return. Next assault was made by Piotr Kępkiewicz in the year 2000. During the dive he removed the choke, but he did not pass through.</p>
<p>There are also sumps in the side streamways in Wielka Śnieżna, which deliver water to the main stream. They can be dived against the current and dry passages beyond the sump must be explored by climbing. One of such sites is a sequence of Krakowski Sump and Magda's Sump. Andrzej Stupak dived through Krakowski Sump (9m long) in 1991. It was a free diving. It was the only time in the history of Polish cave diving, when a sump was explored in free diving style. The next sump (Magda's) was explored using already full equipment in the same year, but no further way in the dry passage was found. Wiktor Bolek and Norbert Ziober undertook the exploration in this place once again in 1999. A 130 m long passage was found after Magda's Sump. It was possible after climbing up in a narrow slit. The last point obtained was 30m higher than the water level in the sump.</p>

<p align=center>
	<img src="./img/overview_3.jpg" alt="Just before dive" height=344 width=500><br>
	<i>Just before dive</i>
</p>

<p>In two other caves containing big sumps, no new underwater terrain was gained during past four years. The exploration achievements in these places were summarised in the special issue of <a href="#res" class="navi">"Jaskinie" ("The Caves")</a> published on the occasion of 12th International Speleological Congress.</p>
<p>Apart from exploration of sumps in Tatras, Polish cave divers took the challenge to explore the sumps in Europe. Krzysztof Starnawski, who, after several attempts, submerged to -180 meters in Hranicka Propast in Czech Republic, made one of the greatest achievements. It is a vertical pit, which was penetrated by a sonda operated from surface to the depth over 200m. But no man dived there deeper than Krzysztof. His dive lasted 4 hours and 21 minutes. He used 11 bottles. Water temperature was 12-14 deg. C.</p>
<p>Other exploration was made in Romania in Bihor Mountains. Wiktor Bolek submerged to -80 meters in the second sump of Izbucul de la Tauz. This sump is a ramp, which goes slowly downwards. Sand and grit fill the bottom. At the depth of -74m there is a squeeze, which stopped previous explorers, but was passed this time. Afterwards the passage opens up and exploration is still possible.</p>

