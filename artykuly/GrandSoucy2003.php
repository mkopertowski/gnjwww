<h1>
	Najgłębsze nurkowanie na aparacie o obiegu zamkniętym w jaskini
</h1>

<p>W aparat nurkowym o obiegu zamkniętym wykonuje się wydech do tzw. pętli oddechowej, w której gaz jest oczyszczany z dwutlenku węgla. Dzięki temu zużycie gazów jest znacznie mniejsze niż jak przy nurkowaniu na aparatach o obiegu otwartym. Aparaty o obiegu zamkniętym (mimo wielu zalet) charakteryzowały się dużą ceną oraz dużą zawodnością, co eliminowało je z użycia w jaskiniach. Jednak postęp technologiczny (w tym zastosowanie sterowania elektronicznego) umożliwiły zwiększenie niezawodności oraz zmniejszenie ceny. Przyszłość eksploracji głębokich i długich syfonów należy właśnie do aparatów o obiegu zamkniętym.
</p>
<p>W języku polskim nie ma dobrego odpowiednika do określania tego typu aparatów. Używa się natomiast angielskiego określenia rebreather. Wobec tego w gronie Podkomisji Nurkowania Jaskiniowego PZA postanowiliśmy wymyśleć krótkie polskie słowo, które oddawałoby istotę tego urządzenia i tak narodziło się określenie <b>ODDYCHARKA</b>.
</p>
<p>Najgłębsze jak do tej pory nurkowanie na oddycharce Mk15.5 w jaskini wykonał Jerome Meynie w listopadzie 2003 w Grand Soucy (Francja, Commune de Saint-Vincent sur l'Isle). Jerome zanurzył się na głebokość -184m, przepływając przy tym odcinek 240 m w poziomie. 
</p>
<p>Jerome zaobserwował trzy termokliny. 8C na -150m, 10C na -50m i 12C przy powierzchni. Podczas zanurzania na helioxie z prędkością 30m/min nie zaobserwował objawów HPNS (High Pressure Nervous System). W czasie nurkowanie widoczność była słaba 3-4 metry. Ze względu na naruszenie osadów stale się pogarszała, tak że przy końcu nurkowania wynosiła zaledwie 50 cm. Przy prawie zupełnym braku widoczności odczytał z VR3 głębokość -184m w 32 minucie nurkowania. Na tej głębokości był 240m od powierzchni. 
</p>
<p>Podczas wynurzania na głębokości -130m, przepływając przez kłęby zmąconej wody, śruba skutera zaplątała się w poręczówkę. Co więcej butle depozytowe również się w nią zaplątały. Jerome nie bawił się na głębokości w prawidłowe procedury wycinania się. Jedną ręką trzymając linkę idącą do góry odciął tę idącą w dół. 
</p>
<p>Na głębokości -100m dopłynął do stromej ściany (klif). W tym momencie pod wodą zeszła lawina z kamieni i mułu. Przytulił się do ściany chowając głowę pod skalną półka. Przez cały czas słyszał jak kamienie uderzają w oddycharkę, która miał na plecach. Po dwóch najdłuższych w jego życiu minutach kamienie przestały spadać. Oddycharka działała poprawnie. Jednak w wyniku lawiny widoczność spadła do zera, a co gorsza Jerome zgubił poręczówkę. Opanował panikę. Gazy, które miał ze sobą były wystarczające do przebywania pod wodą jeszcze przez cztery godziny. Dzięki skuterowi mógł przeszukać spory obszar. Po 15 minutach pływania skuterem odnalazł starą, angielską poręczówkę na głębokości -65m. 
</p>
<p>Na głębokości -18m Jerome miał długi przystanek dekompresyjny, więc postanowił nieco poeksplorować i pokartować na tej głębokości. Dowiązał się do głównej liny i popłynął w bok penetrując różne odnogi i ciasności. W pewnym momencie zorientował się, że linka wychodząca z kołowrotka jest luźna. Zgubił poręczówkę już drugi raz podczas tego nurkowania. Kolejne 20 minut pływania na skuterze zajęło mu odnalezienie właściwej poręczówki. 
</p>
<p>Od głębokości -6m dekompresję wykonywał na zapasowej (włoskiej) oddycharce Castoro C96Pro. 
</p>



