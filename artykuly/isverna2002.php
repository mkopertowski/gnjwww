<h1>
	Wypadek w jaskini Isverna (Rumunia)
</h1>

<p>W dniu 15 listopada 2002 roku w rumuńskiej jaskini Isverna (góry Muntii Mehedinti) zdarzył się wypadek, w którym życie straciła dwójka doświadczonych węgierskich nurków jaskiniowych - <b>Lorand Pazmendy</b> (lat 41) i <b>Gyorgy Szighety</b> (lat 37). Do zdarzenia doszło pomiędzy Zielonym a Żółtym Syfonem. Odcinek łączący te syfony jest prawie poziomy, do pokonania są w zasadzie dwie małe kaskady, które nigdy nie stwarzały najmniejszych problemów.</p>

