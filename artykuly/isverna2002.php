<p>Dokładne okoliczności wypadku jak zwykle nie są do końca znane. Podejrzewa się, że wypadek miał miejsce podczas powrotu nurków wzdłuż strumienia płynącego w galerii pomiędzy syfonami.
Pierwszy z płetwonurków złamał nogę tuż przed jedną ze wspomnianych kaskad. Złamanie było złamaniem otwartym. Najprawdopodobniej płetwonurek ten upadł na twarz, jego udo weszło w szczelinę i zablokowało się. Nie mógł się uwolnić od zestawu butli niesionego na plecach. Głębokość wody w tym miejscu  wynosiła ok. 40 cm, jednakże z powodu wysokiego poziomu wody prąd był bardzo znaczny. Płetwonurek miał założone płetwy. Za przyczynę śmierci podaje się utonięcie.</p>
<p>Drugi płetwonurek, który wracał pierwszy z syfonu i zdążył pokonać kaskadę, zorientował się że jego kolega potrzebuje pomocy. Najprawdopodobniej nie był w stanie już udzielić żadnej pomocy i postanowił wracać przez Zielony Syfon aby zawiadomić pozostałą część ekipy. Zielony Syfon od strony, z której powracał, zaczyna się dwumetrowej głębokości jeziorkiem. Odczyt z komputera nurkowego drugiego płetwonurka wskazuje na to, że po 40 sekundach od wejścia do jeziorka znajdował się już na jego dnie. Dalsze 60 minut to już była linia prosta - aż do wyciągnięcia ciała przez kolegów... Najprawdopodobniej dostał ataku serca - była to osoba wysoka (ok. 2 metrów wzrostu) i w razie jakichkolwiek problemów sprzętowych mogła łatwo osiągnąć powierzchnię jeziorka po prostu wstając. Płetwonurek ten został znaleziony twarzą do góry, z automatem oddechowym w ustach. Kamizelka ratunkowa nie była napompowana. Każda z butli zawierała po 150 bar powietrza. Automaty oddechowe były sprawne.</p>
<p>Pierwszego płetwonurka nie było w stanie wydostać 5 pozostałych osób. Dopiero zespół ratowników jaskiniowych z Tirgu Jiu  ("Salva Mount") po pięciu godzinach pracy za pomocą układów wyciągowych uwolnił zaklinowane ciało przygniecione ekwipunkiem nurkowym.</p>

