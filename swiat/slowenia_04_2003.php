<p>Po jednodniowej przerwie na załatwianie wszystkich formalności, niezbędnych z punktu widzenia uruchomienia bazy nurkowej przez Rafała, ruszyliśmy w drogę powrotną do Polski. Tym razem pojechaliśmy inną trasą, przez Rijekę, z zamiarem zanurkowania w najgłębszym słoweńskim wywierzysku - w Divjim Jezerze. Już przed Rijeką zaczęło lać i to porządnie. Rafał już zaczął wymiękać, sugerując że w górach na pewno jest jeszcze gorzej i nie ma po co jechać. Jednak ostatecznie udało się skłonić Rafała do dalszej jazdy i o 21 byliśmy na parkingu przy wywierzysku.</p>
<p>Wywierzysko jest imponujące, ma wymiary 60m na 40m i znaczną głębokość co najmniej 160m co stawia je w rzędzie najgłębszych na świecie. Odwadnia obszar ponad 125km kwadratowych, a wydajność dochodzi do 100m3 na sekundę. Jest położone ok. 2km na południe od miasta Idrija. Doprowadza do niego asfaltowa leśna droga. Zaraz przy samym wywierzysku znajduje asfaltowy parking z ławeczkami idealny do przebrania. Z parkingu jest około 30m do samej wody idąc brzegiem strumienia. Ponieważ wywierzysko jest położone w parku narodowym a my nie wiedzieliśmy czy tam można nurkować, staraliśmy się przetransportować sprzęt i przebrać możliwie nie wzbudzając niczyjego zainteresowania.</p>

