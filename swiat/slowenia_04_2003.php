<h1>
	Divje Jezero 2003 - Nocne nurkowanie w Słowenii
</h1>

<p>W kwietniu zadzwonił Rafał Górecki i zapytał się czy nie chcę pojechać do Chorwacji na kilka dni. Na pytanie dlaczego, Rafał odpowiedział że nie chce mu się jechać samemu. Więc doszedłem do wniosku że warto wykorzystać okazję do wykonania kilku nurków a być może uda się gdzieś zanurkować pod ziemią. Standardowo podróż na miejsce zajęła nam kilkanaście godzin.</p>
<p>Ja przy okazji postanowiłem przetestować swój rebreather (Buddy Inspiration) po gruntownych przeróbkach, po tym jak Michał Stajszczyk dorobił mi układ do przedmuchiwania sensorów tlenowych. Ja z rebreatherem a Rafał z podwójnym zestawem butlowym w końcu odnaleźliśmy wyspę, jak się później okazało leżącą dość głęboko. W sumie w pierwszy dzień zanurkowaliśmy na głębokość ok. 35m co w pełni nas usatysfakcjonowało jak na pierwsze nurkowanie po długiej przerwie.</p>

<p align=center>
	<img src="./img/slowenia_04_2003_1.jpg" alt="Włodek w Adriatyku" height=295 width=400><br>
