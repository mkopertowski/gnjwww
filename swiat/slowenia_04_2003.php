<p>Założyliśmy że będziemy nurkować nie głębiej niż 40m i nie dalej niż koniec naszej poręczówki, której mieliśmy około 100m. Ja z obiegiem zamkniętym a Rafał standardowo z otwartym. Już na głębokości około 20m metrów okazało się że jaskinia jest zaporęczowana i to całkiem porządną poręczówką rdzeniową o grubości ok.5mm. Sam korytarz jest wyjątkowo przestronny o szerokości ok.10m i średniej wysokości ok. 5m. Woda jest wyjątkowo przejrzysta i nie mącąca się. Dno jest wyścielone grubym żwirem i wielkim głazami. Nasze ponad 100w światła nie było w stanie w pełni rozświetlić zalanych przestrzeni. Po osiągnięciu 40m głębokości i około 1/3 długości syfonu musieliśmy zawrócić jak było umówione, aczkolwiek w tych warunkach osiągnięcie znacznej głębokości wydaje się stosunkowo łatwe. Według Rafała który nurkował we Francji Divje Jezero jest syfonem klasy francuskiej, gdzie woda jest czysta i jest szeroko. Zdecydowanie wywarł na mnie duże wrażenie i utrzymał mnie w przekonaniu że nurkowanie jaskiniowe to jest to.</p>

