<h1>
	Nurkowanie w jaskiniach słoweńskich listopad 2003
</h1>

<p>Wyjazd odbył się w składzie: Andrzej Czubalski, Iza i Darek Lermer oraz Arek Nowak. 
Nasze plany to dwa wywierzyska oraz rozpoznanie terenu, jako miejsca przyszłego działania.
Po obowiązkowej wizycie w Trieście oraz zjedzeniu pizzy jedziemy na miejsce noclegowe. 
Nazajutrz na pierwszy ogień idzie wywierzysko Steberk.
 Wnikamy w czeluść, na początku - faktycznie - mało obszerna, ale w poprzek zestaw 2x10L + ja wchodzi. 
Dziura nie za wielka, lecz bardzo ładna i nie mącąca się. Zgodnie z namiarami FAR używamy tylko specjalnych gazów.
</p>

<p>Zabrany z Warszawy agregat klubowy, mimo, że nie napracował się wiele, okazał się niezbędny i spisuje się niezawodnie. Na tego typu wyjazdy - ideał.
</p>

<p>Przy okazji namierzamy liczne ponory i wywierzyska Cetnickiego Polja.</p>
<p>Następnym obiektem naszej eskapady jest Jama Suchodolca odwiedzana już przez członków naszego klubu. 
Czubal szuka jej w biegu i znajduje.
Dzięki ci GPS... Transport przez las i wnikamy w głąb. Ponieważ dla co poniektórych to pierwsze nurkowanie jaskiniowe, 
zwiedzane groty kończymy po przebyciu trzech syfonów (+/- 200m ciągu jaskini). 
</p>
<p>Nazajutrz, żeby nie zostać zabitym przez żonę (nie nurkującą, ale ubierającą nas i b. b. pomagającą) odwiedzamy Križnu Jamu oraz wąwóz Žela RAK , jak również liczne wywierzyska i ponory tego rejonu. Jako założyciele Federacji FAR robimy sobie pamiątkowe zdjęcie na tle wielkiego mostu na rzece RAK. Zaraz potem penetrujemy wielki ponor rzeki Rak w jaskini Tkalca Jama , ale szybko kończymy penetrację po przeczytaniu tabliczki 'ku pamięci nurka, który spenetrował ją tylko w jedną stronę'..</p>
<p>Zdjęcia podwodne z Jamy Suchodolca zrobiliśmy zwykłą cyfrówką w prostej, wodoszczelnej obudowie i pewnie były by fajniejsze gdyby nie to, że robiliśmy je w drodze powrotnej (zmącenie).</p>

