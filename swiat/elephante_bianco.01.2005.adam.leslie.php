<h1>
	Styczniowy wyjazd do Włoch - Elephante Bianco
</h1>

<p>Zachęceni opowieściami i artykułami  Włodka Szymanowskiego postanowiliśmy wraz z Krzyskiem Mierzeckim zorganizować wyprawę, której głównym celem miało być nurkowanie w jaskini Elephante Bianco leżącej we włoskich Dolomitach.</p>
<p>Jaskinia ta jest najgłębszym włoskim wywierzyskiem i znajduje się w dolinie Val di Brenta.Słynie ona z niezwykłej urody, ogromnej głębokości, wielkich przestrzeni wewnątrz i czystej ciepłej "około 10 stopni" wody.</p>
<p>Przygotowania poszły nam nadspodziewanie sprawnie mimo tego ze dzień wcześniej wróciliśmy z nurkowania w Rumuńskiej Transylwanii gdzie nurkowaliśmy razem ze "Sprytnym" w Izbucul Tauz .Dokładny opis tego wyjazdu jest <a class="navismall" href="http://dnh.e-wro.net/php/content.php?content.117" target="_new">TUTAJ</a>.</p>
<p>Początkowo nasze plany były bardzo ambitne i planowalismy z Krzyśkiem zrobić "stówę", jednak po konsultacjach trochę ochłonęliśmy,zmieszaliśmy trimix i ułożyliśmy runtime na -80m.</p>

