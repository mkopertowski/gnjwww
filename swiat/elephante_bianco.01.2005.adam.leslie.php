<p>Osiemnastego stycznia wyruszyliśmy do Włoch w trzyosobowym składzie, ponieważ dołączył do nas jeszcze Marek Giergiczny(SW). Podróż przebiegła bez problemu, trafiliśmy na miejsce w stylu "on sight". Na miejscu poznaliśmy w knajpie przesympatyczną kelnerkę z Polski i ruszyliśmy obejrzeć wywierzysko, które się okazało leżec w srodku miasteczka Valstagna.</p>
<p>Już w trakcie transportu sprzętu okazało się że nie jesteśmy sami. Tego dnia nurkował jeszcze prezes belgijskiej federacji jaskiniowej, któremu towarzyszył wybitny włoski nurek jaskiniowy Casatti.</p>
<p>Jak sie okazało Belg był niepocieszony ponieważ miał mu towarzyszyć pod wodą grecki filmowiec, który się nie pojawił.</p>
<p>Krzysiek zanurkował pierwszy i po około 80 min wyszedł z wody w doskonałym nastroju zauroczony pięknem jaskini. Następnie Ja zacząłem przygotowywać się do zanurzenia. W tym momencie pojawił się Grek obładowany sprzętem video. Gdy dowiedział się ze jest spóźniony o trzy godziny a nasz poprzednik już zmierza w kierunku Belgii postanowił nie rezygnować z filmowania i płynąć za mną do głębokości około 45m a następnie wrócić i czekać na mnie na moim pierwszym przystanku dekompresyjnym, na którym miałem się pojawić po 20 min i zmienić gazy (31m).</p>

