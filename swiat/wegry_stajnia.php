<h1>
	Stajnia Kleina nurkuje w jaskiniach na Słowacji i Węgrzech
</h1>

<p>W dniach 26 - 27 stycznia 2004 odbyły się kolejne nurkowania jaskiniowe, w których wzięła udział Stajnia Kleina (Robert Klein, Jacek Trembowelski). Tym razem nurkowaliśmy w towarzystwie Sławka Paćko, Anny Rapp oraz grupy Czeskich nurków IANTD z Silvą Peknikiem na czele.</p>
<p><b>Tornala - Słowacja</b></p>
<p>To krater, którego wejście zaczyna się na głębokości około 6 metrów, potem opada w dół w kształcie dzwonu na głębokość prawie 40 metrów. Woda ma temperaturę około 10 stopni gdyż w kraterze znajduje się ujście wód termicznych. Niestety widoczność nie przekraczała 3 metrów. Akwen ten jest znany z bardzo zmiennej widoczności, potrafi się ona zmieniać z dnia na dzień. Nad samym brzegiem znajduje się budynek bazy nurkowej w który mogliśmy się przebrać i zostawic sprzęt. Dojazd samochodem jest możliwy nad sam brzeg krateru.</p>

