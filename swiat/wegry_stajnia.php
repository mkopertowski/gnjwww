<p><b>Heviz - Węgry</b></p>
<p>To kolejne źródło wód termicznych i mineralnych. Temperatura wody na powierzchni przekracza 20 stopni C i wzrasta do 38 stopni w komnacie jaskini w której nurkowaliśmy. Nurkowanie zaczyna się przy stromej ścianie, wzdłóż której opadamy na około 40 metrów aby dostać się do komnaty jaskini. Bijące wewnątrz komnaty źródła powodują, że w wąskim tunelu będącym wejściem do jaskini musimy pokonać silny prąd. Widoczność bardzo dobra, chociaż łatwo zmącić wodę gdyż dno zalega łatwo podnoszący się muł. Komnata jest bardzo obszerna, sprawia imponujące wrażenie. Nurkowanie w tym miejscu organizowane jest przez lokalny klub nurkowy i możliwe jest tylko z przewodnikiem z tego centrum. Opłata wynosi 40 euro. Na miejscu jest możliwe nabicie butli. Wynurzając się napotykamy tablicę wmurowaną w ścianę upamiętniająca nurków, którzy zginęli w tym miejscu.</p>
