<p><b>Tapolca - Węgry</b></p>
<p>Jaskina znajduje się około 10 km od północnego przegu jeziora Balaton, przepływa przez nią źródło wody o wydajności 22 tysięcy litrów na minutę, temperatura wody to 20 stopni C. Woda wydrązyła w skale kilkaset metrów poziomych korytarzy obecnie dostępnych nurkom. Maksymalna głębokość nie przekracza 7 metrów a większość korytarzy znajduje się na głębokosci około 2 metrów. Jaskinia została odkryta przypadkowo w 1902 roku podczas drążenia studni. Obecnie wejście do niej znajduje się w centrum miasta, w budynku a suche jej partie są dostepne zwiedzającym. Warunki panujące w jaskini są sprzyjające nurkowaniu, utrudnieniem jest warstwa mułu zalegającego dno. Konsytencja mułu przypomina glinę, niestety niewielka wysokośc korytarzy dochodząca zaledwie do około 1 metra ułatwia jego podnoszenie. W jaskini dostępnych jest kilka tras możliwych do nurkowania. Każdą z nich można pokonac ponownie dopiero po około 3 godzinach gdy prąd wody usunie zmuloną wodę. Trasy nurkowań są zaporęczowane na stałe.</p>

