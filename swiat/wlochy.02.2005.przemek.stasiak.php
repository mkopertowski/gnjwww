</p>


<p>Nazajutrz Paweł Poręba, Rafał Górecki i Maciek Czykierda powrócili do jaskini. Chwile oczekiwano na włoskiego nurka, który kończył swoje eksploracyjne nurkowanie i przy okazji dostarczył wielu ciekawych informacji n/t jaskini. Obecnie jest ona eksplorowana przez klub GGG Modon, osiągneła długość ponad 1700 metrów i głębokość ponad 120 metrów z dużą szansą na pogłębienie (eksplorację zakończono nad studnią prowadzącą znacznie głębiej). Włoch zdjął nasze oporęczowanie studni wejściowej, gdyż uważał je za niebezpieczne - i faktycznie, już w trakcie wynurzania się  podczas rekonesansu Maciek Czykierda stwierdził, że studnię wejściową (a w zasadzie system poziomych szczelin) można w miarę łatwo pokonać nie kładąc poręczówki, która może wręcz przeszkadzać, lecz pozostawił poręczówkę w celu pokazania kolegom właściwej drogi wejścia w system.</p>

