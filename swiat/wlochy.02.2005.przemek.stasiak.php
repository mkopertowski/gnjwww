<p>Podsumowując, uczestnicy nurkowań w tej jaskini twierdzą, że jest to jedna z najpiękniejszych jaskiń w których nurkowali. Duża rozległość horyzontalna, przy głębokościach nitroksowych, powoduje, że jest idealną jaskinią do ćwiczenia długich dystansowo nurkowań. Główny ciąg jaskini nadaje się do pływania za pomocą skuterów.</p>

