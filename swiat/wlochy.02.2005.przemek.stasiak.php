<h3>Informacje o Grotta dei Fontanazzi</h3>

<p>Równolegle do nurkowań w Elephante Bianco zlokalizowano przez Rafała Góreckiego i Maćka Czykierdę nową dla nas jaskinię <b>Grotta dei Fontanazzi</b>. Jako że studnia wejściowa miałą być dość ciasna, Maciek Czykierda jeszcze w piątek wieczorem zanurkował w konfiguracji bocznej w celu rekonesansu i wykorzystując napoczęte nurkowaniem w Elephante Bianco butle osiągnął 170 metr głównego ciągu.</p>

<p align=center>
	<a href="./img/grotta_dei_fontanazzi.gif" target="_new">
