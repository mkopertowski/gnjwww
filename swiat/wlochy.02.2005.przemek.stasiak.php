<p>Nurkowanie jako pierwszy zaczął Rafał Górecki z dwoma osiemnastkami na plecach. Dotarcie do ciągu głównego, fantastycznie zaporęczowanego kablem elektrycznym i linką fi 6 mm zajęło mu ponad 10 minut, co świadczy o ciasnotach. Po wynurzeniu stwierdził, że choć jest ciasno, to przy odrobinie spokoju jest możliwe nurkowanie nawet razem z depozytami.</p>
<p>Następny zanurkował Paweł Poręba, również z twinsetem, tym razem piętnastek. Obydwoje osiagnęli głębokość rzędu 30 metrów i 250-ty metr głównego ciągu, wracając nim do otworu.</p>

