<h1>
Krym po raz 2-gi czyli wyprawa 2006</h1>

<h3 align=right>Wrzesień 2006</h3>

Po doturlaniu się z Wrocławia na miejsce spotkania w stolicy - co nie było ani miłe ani szybkie - sprawnie przepakowaliśmy dobytek i zrobiliśmy szybkie zakupy niezbędnych w podróży artykułów spożywczych. Start w taką podróż nastroił wszystkich nader optymistycznie i nawet korki na wylocie z Warszawy nie psuły nam znakomitego nastroju, tuningowanego Heinekenem. Granicę osiągneliśmy z dobrymi humorami, co niezwykle ułatwiło nam odprawę pozbawiając stresu i lęku przed ukraińskimi pogranicznikami. Zjedliśmy Pszczólkowi Kasine ciacho i pojechalismy dalej. Pierwszym z celów naszego wyjazdu była Odessa. I była ale cała w deszczu. Korzyści z pobytu jednak odnieśliśmy wszyscy. Pszczółek kupił klawiaturę z ruskimi literkami, a pozostała cześć ekipy lokalne produkty. 
<br><br>
Odessa pożegnała nas wylewnie, ruszyliśmy do głównego celu naszej podróży: Aj Pietri. Na miejsce dotarliśmy późno w nocy. Rankiem rozbiliśmy namioty i zorganizowaliśmy bazę. Po przepakowaniu gratów z busa wytypowaliśmy ochtników, którzy poszli na rekonesans do jaskini.
<br><br>
Poszliśmy w składzie: Lama, Michał, Honzo i ja.
Po ok 40-50 min byliśmy pod otworem. Szybka przebierka zaporęczowanie i ziuu
na pierwszy ogień Lama i ja, przy otworze szykowali się Michał i Honzo.
<br><br>
Rekonesans poszedł w miarę sprawnie - aczkolwiek zamiast trafić do syfonu zrobiliśmy obejście do studni zlotowej (ponoru). Za drugim razem się udało i po krótkotrwałym zachwycie spowodowanym sposobem poręczowania byliśmy nad wodą. Oczywiśce ujebalismy się po uszy błotkiem spływającym w sali z syfonem - ale wiadomo tak być musi i już.  Lama popstrykał trochę - co można obejrzeć na jego stronie - i wróciliśmy na powierzchnię. Po nas do jask weszli Michał i Honzo - też im się podobało :)
<br><br>
Następne dni mijały na transportowaniu sprzętu do jaskini. Cześć z nas robiła transporty do otworu, część zwoziła wory na dół a część zanosiła je pod syfon. Rafi w tym czasie zaliczył szybki kurs poruszania się po sznurkach i zadziałał w jaskini, szkoda ze nie zanurał - bo było warto. Ekipa nurkowa się podzieliła na 2 duety.
Pszczółek i Honzo ekipa nr 1, no i Michał i ja.
<br><br>
Pod wodą wszystko luxio, poreczówka którą Mirek Kopertowski pozostawił rok temu była w idealnym stanie - tak, jakby została położona przed paroma dniami.
Do końca poręczówki Mirka dowiązana jest ruska skalibrowana w magiczny dla mnie sposób. Jaskinia rozwija się w szczeliną w skale, w którą ze wzglęfu na konfigurację w jakich nurkowaliśmy nie udało sie pokonać. Pszczółek przesunął szpulkę o ok 2-3 m czyli przodek jest nasz! -:).
<br><br>
Po zakonczeniu akcji w jaskinii Panja i popraniu czego się dało nad strumieniem wyruszylismy nad morze do Jałty. 
W Jałcie bylismy ok 14, gdzie mielismy troche czasu na spacerek zakupy i zjedzenie czegoś lokalnego. Po południu wystartowalismy na spotkanie z Andriejem (moskwiczaninem) który byl niedaleko u rodzinki.
Koniec końców spotkanie doszło do skutu dnia następnego, a my koczowalismy na plazy i mimo szczerych chęci nie weszlismy do wody. Zimna była jak w Gębczycu. Rano zwineliśmy koczowisko i pojechaliśmy zanurkować w morzu. 
<br><br>
Plan był taki aby zanurać jeszcze w wywierzysku Karasubaszi. Po zalatwieniu pozwolenia w wodociągach (bo jest tam ujecie wody) ruszylismy na miejsce. Na nura miał ochote Pszczólek i Honzo. Honza pusciło i to ladnie
ale nie wziął ze soba kolowrotka wiec nie popłynał dalej.
<br><br>
Nastepnego dnia z rana pakowanie gratów i długi powrót do kraju. 
Świtem byliśmy we Lwowie, 
zjedliśmy śniadanie i trochę pozwiedzaliśmy. Na granicy
odprawa poszła bardzo sprawnie i bez zadnego dawania w łape bylismy w kraju (juz po 40 minutach!). Słowem wszystko pięknie.
<br><br>
W wyjeździe uczestniczyli: Andrzej "Pszczółek" Szerszeń, Lama, Dominik "Honzo" Graczyk, Rafał "Rafi" Koryś, Piotr Fita (ekpia warszawska) oraz Asia "Dżoanka" Szymanowska, Michał Stajszczyk, Paweł "Loti" Mordkowicz (ekipa wrocławska).
