<h3>
	Eksploracja wywierzyska Tauz, część druga (Michał Gignal)
</h3>


<p>Do nurkowania w Tauz przygotowywałem się przez dłuższy czas. Nurkowałem w głębokich i płytkich jaskiniach (opisy wyjazdów do jaskiń w Chorwacji znajdują się na stronie PNJ), ćwiczyłem również głębokie nurkowania na wodach otwartych.
</p>



<p>Po przyjeździe do Rumunii na dzień dobry zetknąłem się z objawami choroby dekompresyjnej u Wiktora. Po konsultacjach z KOMH i decyzją o miesięcznej przerwie w działalności Wiktora zostałem jedynym nurkiem, przygotowanym do prowadzenia działalności eksploracyjnej na głębokości poniżej 80m.  </p>

<p>W ramach adaptacji postanowiłem zanurzyć się w jaskini na głębokość 45m umieszczając po drodze jeden z depozytów. Nurkowanie przebiegło bez żadnych problemów, widoczność: ok. 1m, temperatura wody: 8 stopni. Sama jaskinia zrobiła na mnie bardzo pozytywne wrażenie, bez specjalnych ciasnot z miłym żwirkiem na spągu.</p>


<p align=center>
	<a href="./img/Gignal_Matrix.jpg" target="_new">
		<img align=center src="./img/Gignal_Matrix.jpg" alt="Gignal Matrix. Foto: Wiktor Bolek " height=400 width=570></a><br><br><i>Michał Gignal w samochodzie w drodze do Tauz. Zdjęcie nie zostało poddane żadnej obróbce. Efekt jest wynikiem błędu aparatu fotograficznego. <br>
</i>	</p>

<p>Celem pierwszego głębokiego nurkowania było poprowadzenie nowej poręczówki 
w szerszej części korytarza. Poręczówka została już pociągnięta przez Mirka Kopertowskiego do głębokości 48m i tam czekał na mnie kołowrotek.
Nurkowanie zaplanowałem wg Decoplanera z GF 20-70, do 40m EAN28, poniżej TMX 14/53, dekompresję kolejno na: EAN28 od 40m, EAN50 od 21m i tlen na 6m. Dodatkowo w jaskini na 30m został umieszczony depozyt z powietrzem. Samo nurkowanie przebiegło zgodnie z planem, poręczówkę pociągnąłem do dwóch śledzi na 82m i ok. 10m w prawo, wzdłuż korytarza. Moim zadaniem było również ściągnięcie starej poręczówki, jednak już na dnie doszedłem do wniosku, że zabrałoby mi to zbyt dużo czasu i rozpocząłem wynurzanie. Niestety nie jestem jeszcze szczęśliwym posiadaczem ogrzewania elektrycznego. Czas spędzony na dekompresji dłużył się niemiłosiernie, a ja miałem drgawki z zimna. Po nurkowaniu czułem się zmęczony i, choć nie miałem objawów DCS, to jednak biorąc poprawkę na trudne warunki zdecydowałem się na powtórzenie nurkowania dopiero po dwóch dniach przerwy.
</p>


<p align=center>
	<a href="./img/profil20030812.jpg" target="_new">
		<img align=center src="./img/profil20030812.gif" alt="Profil z Suunto " height=400 width=570></a><br><br>Profil pierszego nurkowania. <br>
	</p>

<p>Dodatkowo postanowiłem wydłużyć kolejne nurkowanie i zabrać ze sobą depozyt z trymiksem. 
Drugie nurkowanie również zaplanowałem według DP z GF 20-70, do 40m EAN30, poniżej depozyt z TMX 15/53, na 80m zostawiłem depozyt, aby podjąć go w drodze powrotnej, dekompresja od 40m na EAN30, od 21m na EAN50 i na 6m tlen.
</p>

<p>Zanurzenie przebiegało spokojnie, dopiero na 40m trochę się zdziwiłem nie ujrzawszy depozytu z TMX'em, który miał tu na mnie czekać. Przetarłem oczy ze zdziwienia i postanowiłem skontrolować jeszcze starą poręczówkę. Okazało się, że omyłkowo ta flaszka trafiła na linkę biegnącą o kilka metrów w bok. Całe zdarzenie kosztowało mnie jakieś trzy minuty. Na 80m zostawiłem ostatni depozyt i popłynąłem wzdłuż poręczówki. Dowiązałem się i pognałem dalej w nieznane. Przykro się jednak rozczarowałem, gdy po ok. 10-15m korytarz zaczął skręcać w lewo. Zamiast długiego korytarza sytuacja zaczynała przypominać kręcenie się w dużej komnacie. W pewnym momencie dostrzegłem otwór w ścianie, który mógł prowadzić do korytarza, nawet na upartego wcisnąłbym się tam z moim twinsetem 2x18L, ale zostawiłem to na inną okazję. Przejrzystość wody, i tak niewielka, spadła znacząco. Mijała 11 minuta poniżej 80 metrów. Po sprawdzeniu manometrów zdecydowałem założyć ostatni punkt, odciąć kołowrotek i zawrócić. Wynurzenie przebiegło bez problemów, ubrany byłem cieplej, niż poprzednio, dzięki czemu nie marzłem już tak bardzo. Ponieważ było to ostatnie nurkowanie podczas tegorocznej ekspedycji, z wody trzeba było zabrać również depozyt ucieczkowy. W pewnym momencie płynąłem z twinsetem 2x18L na plecach, miałem dwie dziesiątki po prawej stronie i dwunastkę z piętnastką po lewej stronie (argonu nie liczę) - czułem się jak mała ciężarówka...
Na szczęście duch zespołowy nie zginął i gdzieś na dwunastym metrze spotkałem Mirka, który zabrał mi część butli. Pozostałe odebrali ode mnie Arnold i Mirek, gdy dyndałem na 6m. Chcę tu serdecznie podziękować Mirkowi za dzielne dotrzymywanie mi towarzystwa podczas dłużących się minut ostatniego przystanku. Dla zabicia czasu prowadziliśmy korespondencję na tabliczce. Jej głównym tematem były trunki, jakimi będziemy świętować zakończenie wyprawy. W sumie nurkowanie trwało 2 godziny 49 minut. Po wyjściu czułem zmęczenie w mięśniach prawej ręki, które na szczęście minęło po krótkim oddychaniu tlenem. Było to nurkowanie dłuższe i bardziej męczące niż poprzednie, a jednak czułem się po nim lepiej.
</p>





