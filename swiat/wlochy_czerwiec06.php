<p>Jarek w tym czasie nurkował w Oliero, silny prąd zmusił go do odwrotu po pokonaniu 100 m korytarza, po południu razem z Anią nurkował w EB.</p>
<p>Znów jedziemy do pizzerii na kolację i podładować akumulatory, dziś właścicielka jakoś krzywo na nas patrzy jak podłączamy ładowarki do czterech latarek, ale pizza smakowała wybornie.</p>
<p>Tego dnia byliśmy przygotowani na wykonanie trzech nurkowań, ale po kolejnym już tego dnia transporcie sprzętu z Fontanazzi do EB postanowiliśmy odpuścić nocne nurkowanie w EB i by tradycji stało się zadość uraczyć się zakupionym włoskim winem.
</p>
<p>Niedzielny poranek zaczyna się bardzo wcześnie, kilka minut po 6 rozpoczynamy ostatnie na tym wyjeździe nurkowanie w EB. Tak jak na poprzednich nurkowaniach zostawiamy butle z tlenem przypięte do poręczówki i znów ruszamy podziwiać górne szczeliny w EB i tym razem odnajdując nowe dla nas zaporęczowane korytarze. Trzeba będzie tu jeszcze wrócić. Po nurkowaniu zwijamy obóz i zaczynamy znosić sprzęt do samochodów, mamy szczęście, akurat na nurkowanie przyjechała trójka Włochów, dokonujemy więc wymiany barterowej, my im pomagamy zanieść sprzęt do góry, oni znoszą nasz na dół. O 10 rozpoczynamy naszą kilkunastogodzinną podróż powrotną.</p>

