<h1>X divers team we Włoszech – czerwiec 2006
</h1>

<p>Przygotowywaliśmy się do wyjazdu do Rumuni i nurkowań w Tauz, Galbenei i Dobrestilor,
 ale jak to zwykle bywa pogoda zweryfikowała nasze plany.Przez 3 tygodnie poprzedzające

tygodnie poprzedzające nasz przyjazd w górach Bihoru padał deszcz i o 
nurkowaniu w Rumunii mogliśmy zapomnieć. W ostatniej chwili decydujemy się 
pojechać ponownie do Włoch (byliśmy tam już w tym roku w długi weekend majowy), 
ostatnim razem jednak trafiliśmy na duży wypływ wody z wywierzysk i nie 
udało się zrealizować wszystkich zaplanowanych nurkowań. 
Plany jak zwykle ambitne, co uda się zrealizować zobaczymy na miejscu, 
tak czy inaczej zapowiadał się bardzo ciekawy przedłużony weekend.</p>

<p>Do Valstagny docieramy we czwartek koło 19 i od razu ruszamy na rekonesans 
dwu wywierzysk w których chcemy zanurkować: Grotta Dei Fontanazzi i Elephante Bianco; tak jak poprzednio załatwione mamy pozwolenie na 
nurkowanie w Oliero, ale to bardziej z myślą o Jarku który tam jeszcze nie 
nurkował, my nie planujemy tam nurkować. Okazuje się że w Fontanazzi poziom 
wody jest niższy o 2 m, a w Elephante o 4 m od tego co zastaliśmy będąc tu w 
maju, już wiadomo że nie będzie wyczuwalnego przepływu wody w wywierzyskach, 
zapowiadają się piękne nurkowania. Po 22 rozpoczynamy nurkowanie w Fontanazzi. 
Normalnie pokonanie początkowych zacisków i dotarcie do głównego ciągu jaskini 
zajmuje ok. 4 minut, tym razem jednak nie chciał mnie puścić ostatni zacisk i 
nie dostałem się do głównego ciągu; po kilku próbach wpasowania się postanawiam 
odpuścić na dzisiaj, krążę jeszcze 30 minut zaglądając w każdą dziurę w partiach 
wejściowych. Tym razem więcej szczęścia mieli Michał i Jarek dotarli do ciągu 
głównego i „zrobili” kawałek korytarza.</p>

