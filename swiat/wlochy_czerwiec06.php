<p>Po kilkunastogodzinnej podróży i nurkowaniu jesteśmy nieźle zmęczeni i po północy rozbijamy namioty przy Fontanazzi na kawałku łąki koło winnicy. Przed 6 rano budzi nas warkot kosiarki do trawy i krzyczący coś Włoch. Widać nie spodobały mu się trzy nasze namioty rozbite na jego łące i wygnieciona trawa, używa magicznego słowa „Karabinieri” więc w 10 minut zwijamy namioty i jedziemy do Elephante. Po drodze w Valstagnie trafiamy na targ, mamy więc na śniadanie świeże sery, pieczywo i owoce, pycha. </p>

