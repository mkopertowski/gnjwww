<p>W piątkowy poranek razem z Michałem zanurzamy się na 70 m w EB oglądając fantastyczne ściany studni zaczynającej się na 50 m. Choć widoczność przekracza 30m, nawet mocno skupione światło hida nie ogarnia przeciwległych ścian studni, jest tak ogromna, dopiero teraz tak naprawdę zdajemy sobie sprawę  co nas ominęło gdy nurkowaliśmy tu poprzednio przy widoczności 5-6 m. Po nas nurkuje Jarek również osiągając 70 m, później dołącza do niego Ania, nurkuje w jeziorku wywierzyska i wpływa kilkadziesiąt metrów do jaskini. Dla niej było to pierwsze nurkowanie jaskiniowe. </p>

<p>Rozbijamy namioty przy samym wywierzysku, na kamieniach które jeszcze nie tak dawno były 2 m pod wodą. Reszta część popołudnia mija spokojnie, ale z przygodami. Na parkingu przy EB przetaczamy tlen i jedziemy do centrum nurkowego w Rosa naładować butle, niestety trafiamy na popołudniową przerwę, trzeba tu będzie wrócić za dwie godziny. Podczas drogi powrotnej do Valstagny źle zabezpieczony w bagażniku naszego kombi twinset przechyla się i wybija tylną szybę. Najważniejsze że twin nie ucierpiał i można na nim nurkować. </p>

<p>Późnym popołudniem nurkujemy w Fontanazzi, po drugiej tego dnia wizycie w Rosa z Jarkiem i Anią pomyliłem drogę wracając do Valstagny, kręcimy się prawie pół godziny po włoskiej wsi zanim udaje się nam dojechać pod wywierzysko. Michał jest od pół godziny pod wodą, więc dziś każdy nurkuje sam. Szybko się z przebieramy i wskakujemy do wody, przed zanurzeniem ponowna kontrola sprzętu i okazuje się że to nie koniec przygód tego dnia, mój hid nie działa. Czekam na wynurzenie Michała by sprawdzić przyczynę awarii, podpinam jego kanister, dobrze że złącza pasują, uff świeci. Mimo że akumulator przeżył jego półtoragodzinne nurkowanie powinien jeszcze trochę pociągnąć na 10 W hidzie, zobaczymy, mam przecież jeszcze dwie latarki zapasowe, więc nie powinno być problemów. Dziś szybko bo już w 4 minucie docieram do głównej poręczówki i zaczynam zachwycać się widokami. Pokonuję 250 m głównego ciągu, robię jumpa i wpływam w boczny korytarz którym płynę aż do następnego rozwidlenia, szybka kontrola manometrów, właśnie zużyłem zapas gazu przeznaczony na penetrację, rozpoczynam więc powolny powrót. Hid gaśnie akurat wtedy gdy kończę dekompresję na ostatnim przystanku. Na tym nurkowaniu dotarłem do Bocca Dello Squalo, Michał do studni Pozzo 2000.</p>

<p>Po nurkowaniu jedziemy do pizzeri na kolację i ładowanie akumulatorów, przy okazji naprawiam kanister latarki, okazało się że rozłączył się jeden z przewodów. </p>

<p>W sobotę Jarek postanawia nurkować w Oliero, ja z Michałem nurkuję rano w EB i postanawiam zrobić trochę zdjęć pod wodą –  wyjście z namiotów i po chwili jesteśmy już w wodzie dopływamy główną poręczówką do 40 m i ruszamy pionowo do góry wzdłuż poręczówki którą wypatrzyłem na poprzednim nurkowaniu. Prowadzi do szczelin w stropie jaskini, rozwidla się w kilku miejscach, pozwala jednak zrobić pętlę i powrócić w innym miejscu do głównej liny. Górna cześć EB jest o wiele piękniejsza od części którą można zobaczyć płynąc po głównej linie poręczowej.       </p>

