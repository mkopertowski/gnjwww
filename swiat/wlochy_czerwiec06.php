<p>No i powtórka z dnia poprzedniego, szybka zwijka i ładowanie butli oraz akumulatorów w Rosa, oraz zniesienie całego sprzętu ponownie do Fontanazzi.</p>

<p>Dziś w planach mamy poszukiwania korytarza Ramo Cieco w Fontanazzi. Zanurzenie przebiega bez problemów i po kilkudziesięciu minutach jesteśmy w miejscu gdzie powinien być nasz dzisiejszy cel, szukamy go bez powodzenia przez chwilę, po czym decydujemy się popłynąć do studni Pozzo Finale – 450 m od wejścia, nie zanurzamy się w niej tylko płyniemy jeszcze kawałek wzdłuż poręczówki na 30 metrach. Rozpoczynamy odwrót po drodze starając się zajrzeć do każdego zakamarka i po blisko półtorej godzinie jesteśmy znów na powierzchni. </p>


