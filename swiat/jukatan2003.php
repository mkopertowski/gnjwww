<h1>
	Nurkowania w Cenotes
</h1>

<p>Wapienna płyta Jukatanu to jedno z najbardziej interesujących miejsc dla nurków jaskiniowych na świecie. Większość znajdujących się na tym obszarze jaskiń zalana jest słodką, deszczową wodą. Jaskinie te zwane cenotes są jedynym rezerwuarem słodkiej wody na półwyspie. Kilometrami ciągnące się cenotes są podziemnymi drogami krążenia wody w wapiennej płycie.</p>
<p>Miejscami jej wypływu są wybrzeża Zatoki Meksykańskiej, Morza Karaibskiego lub podmorskie wywierzyska oddalone od brzegu nawet o kilkaset metrów. Doskonałe warunki do nurkowania w cenotes: temperatura wody ok. 25C, widoczność w błękitnej wodzie do 100m i w większości duże przestrzenie przyciągają na Jukatan nurków z całego świata. Podwodną eksplorację prowadzą tu głównie Amerykanie i Meksykanie, sporadycznie inne nacje. Coraz lepsze rozpoznanie terenu (większość otworów cenotes znajduję się w trudnym do pokonania lesie) i logistyka nurkowań procentuje szybkimi odkryciami kolejnych kilometrów korytarzy. Przykładem może być system Ox Bel Ha, który wyeksplorowano w niespełna 6 lat (maj 1998-marzec 2003), a jego długość przekroczyła 100km. Ox Bel Ha jest obecnie najdłuższą jaskinią Meksyku. Inne jukatańskie cenotes również należą do bardzo długich jaskiń (m.in.):</p>
