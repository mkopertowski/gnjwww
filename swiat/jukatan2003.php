<p>Przy okazji obowiązków zawodowych mogliśmy wraz ze Sławkiem Makarukiem (Warszawski Klub Płetwonurków) w październiku 2003 zanurkować w cenotes. W okolicach Meridy (stan Jukatan) nurkowaliśmy w cenotes: Chaksiinikche' i Chelentun. Jaskinie te położone są na terenie rozległych plantacji agawy. Udogodnieniem jest możliwość przewiezienia sprzętu wagonikami ciągniętymi przez muły. Następnie nurkowaliśmy wraz z naszym meksykańskim przyjacielem Fernando Davilla w okolicah Tulum na tzw. Riviera Maya w cenotes: Cristal, Dos Ojos i Grande (stanowiącej fragment systemu Sac Actun o długości 17km).</p>

