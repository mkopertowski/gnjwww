Optymistycznej na Ukrainie. Naszym celem była eksploracja nurkowa w
"Jeziorku 1 Maja" w Rejonie Zaoziernym (podrejon Miraż).
</p>

<p>
W wyprawie udział wzięli członkowie SW: Beata Michalak, Marcin Bielski,
Katarzyna Barcz i Andrzej Szerszeń oraz Dariusz Bartoszewski i Lucyna
Cieślik z SKTJ oraz niezrzeszona Agata Ślimak. Naszymi gospodarzami byli jak
zwykle liczni i gościnni członkowie klubów Cyklop i Manowce. Spędziliśmy 2
noce na biwaku Siwa Kobyła. Naszymi przewodnikami i pomocnikami w
