pozwalająca przytrzymać się przy wychodzeniu. Dno jeziorka jest gliniaste, a
woda stojąca. Początkowo jest ona przejrzysta, po wejściu nurka natychmiast
mąci się powodując spadek widoczności. Pod wodą korytarz przechodzi w
poziomą szczelinę o znacznej szerokości. Niski strop powoduje, że szczelina
po kilku metrach staje się ciasna, a dalej nie dostępna dla człowieka. W
nurkowaniu kolejno wzieli udział Andrzej, Lucyna i Taras. gospodarzami byli
