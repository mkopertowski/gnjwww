<h1>
	"Nurkowanie w jaskiniach Chorwacji i Slowenii w weekend" 
</h1>
<h3>- to nie tytuł kolejnego podręcznika ze znanej serii, lecz zrealizowany cel mini wyprawy, która odbyła się własnie w weekend 27-29.06.2003.</h3>
<p>Pod wodzą Włodka Szymanowskiego (<i>Spryciuli</i>) udział w wzieli: Michał Gignal, Mirek Kopertowski, Arnold Paliński, Piotr Kępkiewicz. Nurkowaliśmy w: Izvor Sinjac, Suhadolca, Divje Jezero.</p>
<p>Ponieważ Izvor Sinjac nie było do tej pory nurkowane przez Polaków, pokuszę się o krótki opis. 
Dojazd: od miejscowości Plaski w kierunku Saborsko, około 1km skręt w lewo przy drogowskazie "Jezero". Stąd 700m, wąską asfaltową drogą, aż do momentu, gdy przy opuszczonych zabudowaniach, zamieni się w szutrową. Odtąd, już na piechotę w lewo, obok zabudowań, w dół przez pole, 200m, ku trzem jeziorkom, będącym zalanym zapadliskiem krasowym.
</p>
<p>Nurkowanie: W "dnie" lewego jeziora znajduje się studnia, która sprowadza na poziom ~40m. Stąd rozpoczyna swój bieg przestronny, łagodnie opadający korytarz, o prostoliniowym przebiegu, którym po około 150m osiągamy 75m. Tu korytarz skręca w lewo i sprowadza na 85m. Dalej nie byłem. Obecny poziom eksploracji to głębokość 103m osiągnięty przez wyprawę francuską w 1999 roku. Ciekawostką podwodną jest dobrze zachowany drewniany wóz "drabiniasty" na głębokości 42m. Przejrzystość wody: 2-3m, stan poręczowania bardzo dobry. </p>
<p>W jaskini zanurkowała cała nasza ekipa, osiągając głębokości: Arni 30m, Mirek 40m, Michał 50m, Spryciula 70m (obieg zamknięty) i ja 85m (tmx).</p>
<p>Suhadolca i Divje Jezero - opis tych jaskiń znamy z wcześniejszych nurkowań Pszczółka i Spryciuli. Podczas tej wyprawy w Suhadolca zanurkowali Michał i Mirek osiągając koniec jaskini. Stan wody był wybitnie niski, sucha stopą można było dotrzeć do drugiego syfonu, sam drugi syfon zamienił się w jezioro, a trudności pokonania partii miedzy syfonami wzrosły. Woda, w syfonie 3 i 4, nadzwyczaj klarowna.</p>
<p>W Divjim zanurkowaliśmy w składzie Michał, Mirek, Spryciula i ja, osiągając głębokość 50m. 
Wrażenia z nurkowań jak najlepsze. Myślę, że bałkańskie syfony są bardzo atrakcyjną alternatywą w stosunku do francuskich. Ich atutem jest również to, ze można pojechać tam na weekend ;-)
</p>

