<h1>
	 System mocowania butli po bokach
</h1>
<p>Ten sposób mocowania jest przewidziany do ciasnych i krótkich syfonów. Jego głównym założeniem jest możliwość łatwego przypinania i odpinania butli. Ze względu na przewidywane ciasności standardowy jacket nie będzie używany. Wobec tego należy użyć specjalnych taśm do mocowania. Butlę przypina się standardowo w dwu miejscach: do pasa na biodrach i do pętli na klatce piersiowej.</p>
<p>Na pasie biodrowym montuje się półkółko z nierdzewnego pręta za pomocą klamerki również ze stali nierdzewnej. Dzięki temu istnieje możliwość łatwej zmiany położenia półkółka na pasie [ <a class="navi" href="./img/sidemount_1.jpg" target="_new">zdjęcie 1</a> ]. Mocowanie butli na pasie powinno być elastyczne. Do tego celu używa się gumowej linki o średnicy około 5 mm. Z tej linki robi się pętlę o długości dobranej do średnicy butli [ <a class="navi" href="./img/sidemount_2.jpg" target="_new">zdjęcie 2</a> ]. Na tej pętli wiąże się wyblinkę [ <a class="navi" href="./img/sidemount_3.jpg" target="_new">zdjęcie 3</a> ], którą zakłada się na butlę Żeby wyblinka nie przesuwała się po butli dodatkowo unieruchamia się ją za pomocą gumek z dętki [ <a class="navi" href="./img/sidemount_4.jpg" target="_new">zdjęcie 4</a> ]. Ucho wystające z wyblinki ściska się za pomocą plastikowego, samoblokującego się spinacza. Do ucha wpina się karabinek alpinistyczny o gładkim zamku.</p>

