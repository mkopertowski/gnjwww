<p>Na klatce piersiowej zakłada się pętlę z taśmy [ <a class="navi" href="./img/sidemount_5.jpg" target="_new">zdjęcie 5</a> ]. Przekłada się ją przez ramiona i spina karabinkiem na wysokości mostka. Wokół szyjki butli wiąże się pętelkę ze zwykłej linki stilonowej (o średnicy 3-4 mm) za pomocą wyblinki [ <a class="navi" href="./img/sidemount_6.jpg" target="_new">zdjęcie 6</a> ]. W pętelkę wpina się karabinek alpinistyczny, który następnie wpina się w taśmę na piersi, co stanowi drugi punkt mocowania butli.</p>
<p>Taki układ pozwala na chwycenie jedną ręką za ucho gumowej linki, otwarcie palcem karabinka [ <a class="navi" href="./img/sidemount_7.jpg" target="_new">zdjęcie 7</a> ] i wpięcie do półkółka na pasie. Zastosowanie rozciągliwej linki gumowej zamiast zwykłej linki stilonowej pozwala łatwiej trafić karabinkiem do półkółka na pasie. Pod wodą maska ogranicza zwykle na tyle obszar widoczności, tak że to wpięcie wykonuje się po omacku. Ponadto na pasie możemy mieć zamontowane akumulatory, co również utrudnia sięgnięcie karabinkiem do półkółka. Na powierzchni guma znacznie się rozciąga, ale pod wodą ciężar butli zmniejsza się, przez co guma wraca do standardowej długości i butla układa się prawidłowo wzdłuż ciała.</p>
<p>Taki sposób mocowania powoduje, że butla jest luźno przypięta do nurka, co umożliwia łatwiejsze układanie się w ciaśniejszych miejscach w syfonie [ <a class="navi" href="./img/sidemount_8.jpg" target="_new">zdjęcie 8</a> ]. Można odpiąć butle z pasa i pchać je przed sobą, podczas gdy przez cały czas są one przypięte do taśmy na piersi [ <a class="navi" href="./img/sidemount_9.jpg" target="_new">zdjęcie 9</a> ].</p>

