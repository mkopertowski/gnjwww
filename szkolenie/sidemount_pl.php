<p>Do butli wkręca się automat z manometrem [ <a class="navi" href="./img/sidemount_14.jpg" target="_new">zdjęcie 14</a> ]. Wąż od manometru układa się pod gumkami na butli, tak by skala manometru znajdowała się na wysokości zaworu i tak by łatwo można było odczytywać wartość ciśnienia. Wąż od drugiego stopnia automatu można również włożyć pod gumki w czasie, gdy butla jest nieużywana. Wyciągnięcie węża spod gumek następuje za pomocą jednego ruchu. Drugi stopień automatu przypina się standardowo do gumki na szyi [ <a class="navi" href="./img/sidemount_15.jpg" target="_new">zdjęcie 15</a> ]. W celu uniknięcia zakręcania i odkręcania zaworu, gdy butla jest już przygotowana do nurkowania, stosuje się na wężu drugiego stopnia złączkę odcinającą FCD [ <a class="navi" href="./img/sidemount_16.jpg" target="_new">zdjęcie 16</a> ]. Ponieważ zawory butli są chronione ramionami nurka, to stosowanie osłon nie jest konieczne.</p>

