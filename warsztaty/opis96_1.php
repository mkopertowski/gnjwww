<h1>
	Francuscy instruktorzy w Zakopanem
</h1>


<p>W dniach 27.12.1996 - 02.01.1996 odbył się w Zakopanem Pierwszy Międzynarodowy Staż Nurkowania Jaskiniowego. Jako instruktorzy zostali zaproszeni Francuzi : Joelle Locatelli, Philippe Bigeard, Jean-Michele Hautvoine, Philippe Moya. Ze strony polskiej staż organizowali uczestnicy wyjazdu do Espinieres - Orniac latem 1995, tj. Wiktor Bolek, Michał Stajszczyk, Krzysztof Starnawski. Pod nadzorem Francuzów odbywali teraz staż przedinstruktorski. 
<p>Według nadasłanych zgłoszeń w stażu miała uczestniczyć ogromna liczba, bo aż 20, kursantów. Jednak o umówionej godzinie w Zakopanem zjawiło się kilkanaście osób oraz zaledwie cztery pełne komplety sprzętu. Stąd nie wszyscy byli w stanie uczestniczyć w zajęciach praktycznych i musiał nastąpić podział na grupy. W pełnym zakresie szkolenia wzięli ostatecznie udział : Witek Cikowski TOPR, Ewa Chrzanowska Active Divers, Adam Domanasiewicz SG KW Wrocław, Jacek Górski Active Divers, Wojciech Kwidziński SKTJ Sopot, Rafał Szaniawski SW Warszawa. W części teoretycznej uczestniczyli także grotołazi z Zakopanego. 

<p align=center>
	<img src="./img/zakopane96_1.jpg" alt="Nurkowanie pod lodem podczas warsztatów" height=316 width=213><br>
	<i>Nurkowanie pod lodem podczas warsztatów</i>
</p>

<p>Przeprowadzono wykłady z następujących tematów : niebezpieczeństwa środowiska, sprzęt, poręczówka i poręczowanie, obliczanie powietrza, kartowanie. 
<p>Początkowo planowaliśmy, że zajęcia praktyczne odbędą się w Morskim Oku oraz w jaskiniach Dudnica i Kasprowa Niżnia. Jednakże piersze nurkowanie Francuzi postanowili przeprowadzić na basenie krytym, aby zorientować się w naszym sprzęcie i umiejętnościach. Wykorszystaliśmy do tego celu pływalnię w Centralnym Ośrodku Sportu, o głebokości 3.6m. Na dnie ustawiono tor z poręczówki, którą stabilizowano z ciężarków oraz z drabinek basenowych. Wykonywano następujące ćwiczenia : poruszanie się wzdłuż linki z maską i bez, poszukiwanie oraz wycinanie się z poręczowki, poręczowanie i reporęczowanie fragmentów toru. 

<p align=center>
	<img src="./img/zakopane96_4.jpg" alt="Po zajęciach basenowych" height=259 width=364><br>
	<i>Po zajęciach basenowych</i>
</p>

<p>Na tym etapie Francuzi stwierdzili, że prowadzenie akcji szkoleniowych w naszych jaskiniach byłoby zbyt niebezpieczne. Postanowiliśmy, że dalsze ćwiczenia adbędą się pod lodem w Morskim Oku. Jednak taki wyjazd nie był jeszcze w pełni przygotowany organizacyjnie i zdecydowaliśmy się wieczorem na wyjazd do Krakowa, do kamieniołomu na Zakrzówku. 

<p align=center>
	<img src="./img/zakopane96_2.jpg" alt="Wybijanie przerębla" height=280 width=424><br>
	<i>Wybijanie przerębla</i>
</p>

<p>Zjawiliśmy się tam o 19, w pełnych ciemnościach rozświetlanych jedynie reflektorami samochodów. Woda była pokryta kikunastocentymetrowej grubości lodem, temperatura powietrza wynosiła - 21 stopni Celsjusza. W tych warunkach rozpoczęliśmy akcję od wykucia dwóch przerębli. Zanurzenia odbywały się w trójkach : kursant, stażysta i nadzorujący instruktor z Francji. Pod wodą przeprowadzono w zasadzie jedynie ćwiczenia polegające na poruszaniu się wzdluż poręczówki. Głównym problemem okazało się być samo zanurzenie, gdyż w tych warunkach automaty zamarzały od razu i trzeba było czekać kilka minut w lodowatej wodzie, żeby odtajały. Dobrym patentem okazał się pomysł Adama, by tuż przed wejściem do wody zalewać gorącą herbatą drugi stopień automatu. Zapobiegało to zamarznięciu przy pierszym wdechu.

<p align=center>
	<img src="./img/zakopane96_3.jpg" alt="Nurek przymarza do lodu" height=282 width=426><br>
	<i>Nurek przymarza do lodu</i>
</p>

<p>W trakcie zajęć mieliśmy wizytę grupy antyterrorystycznej, powiadomionej o naszej obecności przez czujnych mieszkańców grodu Kraka. Policjanci z pewnym zaskoczeniem patrzyli na nas, nasz sprzęt i na to, co robimy w tym miejscu o takiej porze i w takich warunkach. 
<p>Końcowa ocena zajęć była jednak negatywna. Zbyt dużo czasu i uwagi trzeba było poświęcać samemu zanurzeniu, a zbyt mało właściwym ćwiczeniom z poręczówką. Pozostałe nurkowania wykonywaliśmy już tylko na basenie w cieplarnianych warunkach. Do Morskiego Oka pojechaliśmy tylko na wycieczkę. 

<p align=center>
	<img src="./img/zakopane96_5.jpg" alt="Po nurkowaniu" height=258 width=386><br>
	<i>Po nurkowaniu</i>
</p>

<p>Co prawda nie udało się zorganizować naszego kursu na takim poziomie jak we Francji, ale zrobiliśmy wszystko, co było możliwe w naszych warunkach. Najważniejsze, że spora część zainteresowanych osób miała sposobność poraz pierwszy zetknąć się z zaawansowanymi i przemyślanymi technikami nurkowania w jaskiniach.

<h3 align=right>Wiktor Bolek</h3>
