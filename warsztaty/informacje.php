<h3>Etap 1. &nbsp&nbsp&nbsp TEORIA i PRAKTYKA</h3>

Wrocław 3-4.06.2006<br>
W czasie dwóch dni uczestnicy opanują niezbędną wiedzę podczas zajęć teoretycznych, przećwiczą wszystkie techniki podczas zajęć praktycznych 'na sucho', a następnie na basenie i/lub kamieniołomie w Gębczycach  przetrenują je w pełnym sprzęcie nurkowym.
Koszt 300 zł. Dla grotołazów - posiadaczy Karty Taternika zrzeszonych w PZA 50 % zniżki.

<h3>Etap 2. &nbsp&nbsp&nbsp JASKINIE</h3>

<b>Wariant A - wywierzyska Słowenii</b><br>
Słowenia 15-18.06.2006 (4 dni, święto Boże Ciało)<br>
W programie 3 nurkowania jaskiniowe, praktyczne zastosowanie wiedzy z etapu 1. W planie nurkowania w jaskiniach Jama Bilpa, Suchadolca, Steberk.
Koszt 400 zł. Dla grotołazów - posiadaczy Karty Taternika zrzeszonych w PZA 50 % zniżki.
<br>
<br>
<b>Wariant B - syfony Tatr</b><br>
(tylko dla posiadaczy Karty Taternika)<br>
Zakopane 24-25.06.2006<br>
W programie 3 nurkowania, praktyczne zastosowanie poznanych wcześniej technik i ćwiczenia.
W planie Jaskinia Kasprowa, Zimna, Dudnica - 3 nurkowania, praktyczne zastosowanie poznanych wcześniej technik.
Koszt 200 zł.


