<p>Teraz trzeba zacząć myśleć o przyszłym roku. Warsztaty niewątpliwie zdały egzamin jako impreza cykliczna co potwierdzają coraz liczniejsi uczestnicy. Zapewne jak zawsze można wiele usprawnić wyciągając wnioski z zebranych dotychczas doświadczeń. Konstruktywne uwagi od uczestników jak zawsze mile widziane. W końcu każdy kto pojawia się na warsztatach  w pewien sposób je tworzy swoim udziałem. Do zobaczenie w przyszłym roku!!</p>

