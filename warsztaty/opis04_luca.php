<p>Długi weekend czerwca dla wszystkich osób związanych z nurkowaniem jaskiniowym jest wyjątkowy. W tym roku już po raz kolejny odbyły się Warsztaty Nurkowania Jaskiniowego zorganizowane przez Podkomisję NJ. Ośrodek w Białym Kościele gościł w dniach 9-13 czerwca aż trzydziestu  zapaleńców.</p>

