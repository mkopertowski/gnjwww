<p>Dla wielu taki sposób działania i same techniki były zapewne nowością. Przećwiczono m.in. kartowanie (sporządzanie planów), poręczowanie, korzystanie z butli depozytowych, autoratownictwo (czyli tzw. wycinanie i szukanie zagubionej poręczówki) oraz pływanie bez płetw w konfiguracji bocznej.</p>
<p>W tym roku z wielu powodów trzeba było zrezygnować z nurkowań w sztolniach. O wiele bardziej istotne było rzetelne opanowanie nowych praktycznych umiejętności. A na ich wykorzystanie przyjdzie z pewnością jeszcze pora. Ważne było też uświadomienie, że wszelkie akcje eksploracyjne związane z nurkowaniem jaskiniowym to przede wszystkim przedsięwzięcie logistyczne. Mimo, że samo nurkowanie to niejako z definicji działanie solo to zazwyczaj jego przeprowadzenie jest uzależnione od wielodniowej, sprawnej  pracy całego zgranego zespołu  ludzi.</p>
<p>Warto również zaznaczyć, że dzięki pracy Włodka (Spryciuli) powstały cenne materiały zawierające dużą porcję teorii. Takie całościowe ujęcie zagadnień dotyczących nurkowania jaskiniowego jest, jak się okazuje, niezwykle potrzebne. Dzięki jego zaangażowaniu powstanie z pewnością w przyszłości wspaniałe wyczerpujące opracowanie.</p>

