<h1>
	Warsztaty Nurkowań Jaskiniowych GĘBCZYCE 2005 
</h1>

<p>To prawda… to już dziesiąty raz! Rozważania dotyczące po prostu fajnych imprez w kategoriach rocznic jest jakoś nie na miejscu ale i tak skłania do pewnych refleksji. Wręcz trudno uwierzyć ile osób „przewinęło się” przez warsztaty. Zmieniały się miejsca, ludzie, możliwości, realia… ale jedno pozostało takie samo. Zawsze pojawiali się tutaj ludzie, którzy chcieli poznać jaskinie. Dla jednych ten temat był nowy dla innych nie, i chcieli skonfrontować swoją wiedzę i doświadczenie z trochę innym podejściem. Co każdy wyniósł z tych spotkań? Należałoby raczej zapytać każdego z osobna. Na pewno przynajmniej niektórym otworzyły one ścieżkę do poznania jaskiń „dalej” – od miejsca, w którym dochodzi się do syfonu a dalej nic nie wiadomo... Inni przyjechali tylko po to żeby przekonać się, że nawet jeśli… to nie tędy droga a część sam temat przerósł już na wstępie i pewnie nigdy w życiu nie zobaczą fajnej dziury. Tak czy tak, dla nikogo te spotkania nie pozostały obojętne. I o to właśnie chodzi!</p>
	
<p>A jak było tym razem? Kameralnie, sympatycznie… Warsztaty odbyły się w terminie 26-29.05.2005 /jak z resztą od kilku lat, w czasie długiego weekendu związanego ze świętem Bożego Ciała/. Uczestnikami w tym roku byli: Lucyna (Luca) Cieślik (SKTJ), Rafał Górecki (SGW), Mirek Kopertowski (SGW), Paweł (Loti) Mordkowicz (SGW), Michał Stajszczyk (SGW), Andrzej (Pszczółek) Szerszeń (SW), Piotr (Płetwa) Dąbrowski, Konrad Dubiel, Dominik (Honzo) Graczyk, Iza (Kapusta) Kapuścianek, Jarek Kur (AGH Kraków), Bartek Lizak, Mirek Wróż oraz z poprzedniego roku: Janusz Gąsecki i Paweł Zimnicki. Ponadto na warsztatach zagościli również: Beata Bolek (SGW), Marysia Szymanowska, Rysia Szymanowska oraz Hanka Buczkowska (SGW) i Bartek Grynda.</p>
	
<p>Tym razem baza została przeniesiona do ośrodka agroturystycznego „Nietoperek” w samych Gębczycach, bardzo niedaleko kamieniołomów. Takie posunięcie bardzo usprawniło organizację i skróciło istotnie dojazdy na nurkowanie. Mieliśmy tam też świetne możliwości do przeprowadzenia ćwiczeń na sucho i zajęć teoretycznych, które przeradzały się czasami w ciekawą dyskusję. Nurkowania odbyły się oczywiście w kamieniołomie w Gębczycach. </p>
	
<p>Wieczory można było poświęcić integracji przy ognisku ale też dyskusjom i opowieściom z różnych wypraw m.in. do Rumunii, Bośni, Chorwacji, Czarnogóry, Włoch, Ukrainy. Tematy się nie kończyły. Oczywiście udało się też posnuć trochę interesujących planów na przyszłość. A co z realizacją? Czas pokaże.</p> 

<p>Równolegle do prowadzonych zajęć wydarzyło się też kilka ciekawych rzeczy. Bartek Grynda umożliwił przetestowanie pod wodą kilku swoich latarek. Hanka Buczkowska zbierała materiały do publikacji w  miejscowej prasie. Kilku osobom udało się przeprowadzenie sprawnej nocnej akcji w kopalni Wapiennik w Nowej Wsi Kłodzkiej.</p>

<p>Każdy dawał z siebie wszystko. Jednak to, że bardzo brakowało w Gębczycach kolejnej osoby odczuwali wszyscy – Włodka (Spryciuli) Szymanowskiego. To On zawsze dawał z siebie najwięcej.</p>

